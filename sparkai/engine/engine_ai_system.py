@dataclass
class BTBlackboard:
    """Shared key-value data store for behavior tree execution.

    Allows nodes to communicate state across the tree without
    tight coupling. Supports nested scopes and signal queuing.
    """

    id: str = field(default_factory=lambda: uuid.uuid4().hex)
    tree_id: str = ""
    entries: Dict[str, Any] = field(default_factory=dict)
    signals: List[str] = field(default_factory=list)
    timers: Dict[str, float] = field(default_factory=dict)
    scope_stack: List[Dict[str, Any]] = field(default_factory=list)

    def get(self, key: str, default: Any = None) -> Any:
        return self.entries.get(key, default)

    def set(self, key: str, value: Any) -> None:
        self.entries[key] = value

    def has(self, key: str) -> bool:
        return key in self.entries

    def delete(self, key: str) -> None:
        self.entries.pop(key, None)

    def push_scope(self) -> None:
        self.scope_stack.append(dict(self.entries))

    def pop_scope(self) -> None:
        if self.scope_stack:
            self.entries = self.scope_stack.pop()

    def emit_signal(self, signal_name: str) -> None:
        self.signals.append(signal_name)

    def consume_signals(self) -> List[str]:
        result = list(self.signals)
        self.signals.clear()
        return result

    def set_timer(self, key: str, duration: float) -> None:
        self.timers[key] = _time_module.time() + duration

    def is_timer_expired(self, key: str) -> bool:
        if key not in self.timers:
            return True
        return _time_module.time() >= self.timers[key]

    def clear(self) -> None:
        self.entries.clear()
        self.signals.clear()
        self.timers.clear()
        self.scope_stack.clear()

    def to_dict(self) -> Dict[str, Any]:
        return {
            "id": self.id,
            "tree_id": self.tree_id,
            "entries": dict(self.entries),
            "signals": list(self.signals),
            "timers": dict(self.timers),
            "scope_depth": len(self.scope_stack),
        }


