            self.state_machine.reset()
            self._total_agents_spawned = 0
            self._total_profiles_created = 0
            self._total_ticks = 0
            self._total_cpu_ms = 0.0
            self._is_debug_visualization_enabled = False
            self._debug_overlay_data.clear()


