class AIPerceptionSystem:
    """AI perception system for vision, hearing, memory, and threat assessment.

    Manages per-agent perception configurations, sensory queries,
    memory of perceived entities, threat level computation, interest
    scoring, and perception event generation for reactive AI.

    Usage:
        perc = AIPerceptionSystem()
        perc.register_agent("agent_1", vision_range=50, vision_angle=120)
        events = perc.update_agent("agent_1", position, rotation, world_entities)
        memory = perc.get_memory("agent_1")
        threat = perc.assess_threat("agent_1", "enemy_1")
    """

    def __init__(self) -> None:
        self._configs: Dict[str, PerceptionConfig] = {}
        self._memories: Dict[str, Dict[str, PerceptionMemory]] = {}
        self._event_queues: Dict[str, List[PerceptionEvent]] = {}
        self._world_entities: Dict[str, Dict[str, Any]] = {}
        self._total_events_generated: int = 0
        self._total_agents_registered: int = 0

    # ------------------------------------------------------------------
    # Agent Registration
    # ------------------------------------------------------------------

    def register_agent(
        self,
        agent_id: str,
        vision_range: float = DEFAULT_VISION_RANGE,
        vision_angle: float = DEFAULT_VISION_ANGLE,
        hearing_range: float = DEFAULT_HEARING_RANGE,
        hearing_attenuation: float = 0.5,
        smell_range: float = 10.0,
        touch_range: float = 2.0,
        memory_duration: float = DEFAULT_MEMORY_DURATION,
        threat_threshold: float = DEFAULT_THREAT_THRESHOLD,
        interest_threshold: float = DEFAULT_INTEREST_THRESHOLD,
        max_memory_entries: int = MAX_PERCEPTION_MEMORY,
        occlusion_enabled: bool = True,
    ) -> None:
        """Register an AI agent with the perception system.

        Args:
            agent_id: Unique agent identifier.
            vision_range: Maximum vision distance in world units.
            vision_angle: Vision cone angle in degrees.
            hearing_range: Maximum hearing distance in world units.
            hearing_attenuation: Sound attenuation factor (0-1).
            smell_range: Maximum smell detection distance.
            touch_range: Maximum touch detection distance.
            memory_duration: Seconds before a memory entry decays.
            threat_threshold: Minimum threat level to trigger alerts.
            interest_threshold: Minimum interest level to gain attention.
            max_memory_entries: Maximum remembered entities.
            occlusion_enabled: Whether line-of-sight checks are used.
        """
        _time_module.sleep(0.001)
        config = PerceptionConfig(
            agent_id=agent_id,
            vision_range=vision_range,
            vision_angle=vision_angle,
            hearing_range=hearing_range,
            hearing_attenuation=hearing_attenuation,
            smell_range=smell_range,
            touch_range=touch_range,
            memory_duration=memory_duration,
            threat_threshold=threat_threshold,
            interest_threshold=interest_threshold,
            max_memory_entries=max_memory_entries,
            occlusion_enabled=occlusion_enabled,
        )
        self._configs[agent_id] = config
        self._memories.setdefault(agent_id, {})
        self._event_queues.setdefault(agent_id, [])
        self._total_agents_registered += 1

    def unregister_agent(self, agent_id: str) -> None:
        """Remove an agent from the perception system."""
        self._configs.pop(agent_id, None)
        self._memories.pop(agent_id, None)
        self._event_queues.pop(agent_id, None)

    def update_agent_config(
        self,
        agent_id: str,
        **kwargs: Any,
    ) -> bool:
        """Update perception configuration for an agent.

        Args:
            agent_id: Target agent identifier.
            **kwargs: Config fields to update.

        Returns:
            True if the agent was found and updated.
        """
        config = self._configs.get(agent_id)
        if config is None:
            return False
        for key, value in kwargs.items():
            if hasattr(config, key):
                setattr(config, key, value)
        return True

    # ------------------------------------------------------------------
    # World Entity Management
    # ------------------------------------------------------------------

    def register_world_entity(
        self,
        entity_id: str,
        entity_type: str = "",
        position: Tuple[float, float, float] = (0.0, 0.0, 0.0),
        velocity: Tuple[float, float, float] = (0.0, 0.0, 0.0),
        threat_base: float = 0.0,
        interest_base: float = 0.0,
        sound_radius: float = 0.0,
        is_visible: bool = True,
        metadata: Optional[Dict[str, Any]] = None,
    ) -> None:
        """Register a world entity that can be perceived by AI agents.

        Args:
            entity_id: Unique entity identifier.
            entity_type: Optional type classification.
            position: World-space position.
            velocity: Movement velocity.
            threat_base: Base threat level (0-1).
            interest_base: Base interest level (0-1).
            sound_radius: Radius of sound emission.
            is_visible: Whether the entity is visible (not hidden).
            metadata: Optional additional data.
        """
        self._world_entities[entity_id] = {
            "entity_id": entity_id,
            "entity_type": entity_type,
            "position": position,
            "velocity": velocity,
            "threat_base": threat_base,
            "interest_base": interest_base,
            "sound_radius": sound_radius,
            "is_visible": is_visible,
            "metadata": metadata or {},
        }

    def update_world_entity(
        self,
        entity_id: str,
        **kwargs: Any,
    ) -> bool:
        """Update a world entity's state."""
        if entity_id not in self._world_entities:
            return False
        self._world_entities[entity_id].update(kwargs)
        return True

    def remove_world_entity(self, entity_id: str) -> bool:
        """Remove a world entity."""
        if entity_id in self._world_entities:
            del self._world_entities[entity_id]
            return True
        return False

    # ------------------------------------------------------------------
    # Perception Update
    # ------------------------------------------------------------------

    def update_agent(
        self,
        agent_id: str,
        position: Tuple[float, float, float],
        rotation: Tuple[float, float, float],
        entities: Optional[List[Dict[str, Any]]] = None,
    ) -> List[PerceptionEvent]:
        """Run a full perception update for an agent.

        Evaluates vision, hearing, smell, and touch for all known
        world entities. Updates memory, generates events, and decays
        stale memories.

        Args:
            agent_id: The agent to update.
            position: Agent's current world position.
            rotation: Agent's current rotation (pitch, yaw, roll).
            entities: Optional override list of world entities.

        Returns:
            List of PerceptionEvents generated during this update.
        """
        _time_module.sleep(0.001)
        config = self._configs.get(agent_id)
        if config is None:
            return []

        events: List[PerceptionEvent] = []
        memory = self._memories.setdefault(agent_id, {})
        now = _time_module.time()

        if entities is None:
            entities = list(self._world_entities.values())

        # Decay old memories
        expired = []
        for eid, mem in memory.items():
            if now - mem.last_seen_at > config.memory_duration:
                expired.append(eid)
        for eid in expired:
            mem = memory.pop(eid)
            events.append(PerceptionEvent(
                agent_id=agent_id,
                event_type=PerceptionEventType.MEMORY_DECAYED.value,
                source_entity_id=eid,
                source_position=mem.last_position,
                confidence=mem.detection_confidence,
                sense=PerceptionSense.VISION.value,
            ))

        for entity in entities:
            eid = entity.get("entity_id", "")
            if eid == agent_id:
                continue

            epos = entity.get("position", (0.0, 0.0, 0.0))
            evel = entity.get("velocity", (0.0, 0.0, 0.0))
            etype = entity.get("entity_type", "")
            threat_base = entity.get("threat_base", 0.0)
            interest_base = entity.get("interest_base", 0.0)
            sound_radius = entity.get("sound_radius", 0.0)
            is_visible = entity.get("is_visible", True)

            dist = self._distance_3d(position, epos)

            is_detected = False
            detection_confidence = 0.0
            sense_used = ""

            # Vision check
            if is_visible and dist <= config.vision_range:
                in_cone = self._is_in_vision_cone(
                    position, rotation, epos, config.vision_angle,
                )
                if in_cone:
                    # Confidence falls off with distance
                    confidence = 1.0 - (dist / config.vision_range)
                    if confidence > detection_confidence:
                        detection_confidence = confidence
                        sense_used = PerceptionSense.VISION.value
                        is_detected = True

            # Hearing check
            effective_hearing_range = config.hearing_range
            if sound_radius > 0:
                effective_hearing_range = config.hearing_range + sound_radius * config.hearing_attenuation
            if dist <= effective_hearing_range and sound_radius > 0:
                confidence = 1.0 - (dist / effective_hearing_range)
                if confidence > detection_confidence:
                    detection_confidence = confidence
                    sense_used = PerceptionSense.HEARING.value
                    is_detected = True

            # Smell check
            if dist <= config.smell_range:
                confidence = 1.0 - (dist / config.smell_range)
                if confidence > detection_confidence:
                    detection_confidence = confidence
                    sense_used = PerceptionSense.SMELL.value
                    is_detected = True

            # Touch check
            if dist <= config.touch_range:
                detection_confidence = 1.0
                sense_used = PerceptionSense.TOUCH.value
                is_detected = True

            if not is_detected:
                # Mark as not visible
                if eid in memory:
                    memory[eid].is_visible = False
                    memory[eid].is_audible = False
                continue

            # Compute threat and interest
            threat = threat_base
            if dist > 0:
                threat *= (1.0 - min(1.0, dist / config.vision_range))
            interest = interest_base
            if eid not in memory:
                interest += 0.3  # Novelty bonus

            # Update or create memory
            prev_memory = memory.get(eid)
            if prev_memory is None:
                memory[eid] = PerceptionMemory(
                    entity_id=eid,
                    entity_type=etype,
                    last_position=epos,
                    last_velocity=evel,
                    first_seen_at=now,
                    last_seen_at=now,
                    detection_confidence=detection_confidence,
                    threat_level=threat,
                    interest_level=interest,
                    times_spotted=1,
                    is_visible=True,
                    is_audible=(sense_used == PerceptionSense.HEARING.value),
                )
                events.append(PerceptionEvent(
                    agent_id=agent_id,
                    event_type=PerceptionEventType.ENTITY_SPOTTED.value,
                    source_entity_id=eid,
                    source_position=epos,
                    confidence=detection_confidence,
                    threat_level=threat,
                    interest_level=interest,
                    sense=sense_used,
                ))
            else:
                prev_memory.last_position = epos
                prev_memory.last_velocity = evel
                prev_memory.last_seen_at = now
                prev_memory.detection_confidence = detection_confidence
                prev_memory.threat_level = threat
                prev_memory.interest_level = interest
                prev_memory.times_spotted += 1
                prev_memory.is_visible = True
                prev_memory.is_audible = (sense_used == PerceptionSense.HEARING.value)

            # Threat event
            if threat >= config.threat_threshold and (
                prev_memory is None or prev_memory.threat_level < config.threat_threshold
            ):
                events.append(PerceptionEvent(
                    agent_id=agent_id,
                    event_type=PerceptionEventType.THREAT_DETECTED.value,
                    source_entity_id=eid,
                    source_position=epos,
                    confidence=detection_confidence,
                    threat_level=threat,
                    sense=sense_used,
                ))

            # Interest event
            if interest >= config.interest_threshold and (
                prev_memory is None or prev_memory.interest_level < config.interest_threshold
            ):
                events.append(PerceptionEvent(
                    agent_id=agent_id,
                    event_type=PerceptionEventType.INTEREST_GAINED.value,
                    source_entity_id=eid,
                    source_position=epos,
                    confidence=detection_confidence,
                    interest_level=interest,
                    sense=sense_used,
                ))

            # Sound event
            if sense_used == PerceptionSense.HEARING.value and (
                prev_memory is None or not prev_memory.is_audible
            ):
                events.append(PerceptionEvent(
                    agent_id=agent_id,
                    event_type=PerceptionEventType.SOUND_HEARD.value,
                    source_entity_id=eid,
                    source_position=epos,
                    confidence=detection_confidence,
                    sense=sense_used,
                ))

        # Enforce memory limit
        if len(memory) > config.max_memory_entries:
            sorted_mem = sorted(
                memory.items(),
                key=lambda kv: kv[1].last_seen_at,
            )
            for eid, _ in sorted_mem[:len(memory) - config.max_memory_entries]:
                del memory[eid]

        # Queue events
        self._event_queues.setdefault(agent_id, []).extend(events)
        self._total_events_generated += len(events)
        return events

    def _is_in_vision_cone(
        self,
        agent_pos: Tuple[float, float, float],
        agent_rot: Tuple[float, float, float],
        target_pos: Tuple[float, float, float],
        vision_angle: float,
    ) -> bool:
        """Check if a target is within the agent's vision cone."""
        dx = target_pos[0] - agent_pos[0]
        dy = target_pos[1] - agent_pos[1]
        dz = target_pos[2] - agent_pos[2]
        dist = math.sqrt(dx * dx + dy * dy + dz * dz)
        if dist < 1e-8:
            return True

        # Forward direction from yaw
        yaw = math.radians(agent_rot[1])
        forward_x = math.cos(yaw)
        forward_z = math.sin(yaw)

        to_target_x = dx / dist
        to_target_z = dz / dist

        dot = forward_x * to_target_x + forward_z * to_target_z
        half_angle = math.radians(vision_angle / 2.0)
        return dot >= math.cos(half_angle)

    # ------------------------------------------------------------------
    # Threat Assessment
    # ------------------------------------------------------------------

    def assess_threat(
        self,
        agent_id: str,
        target_entity_id: str,
    ) -> float:
        """Assess the threat level of a target entity for an agent.

        Combines the entity's base threat, distance, velocity toward
        the agent, and memory confidence into a single threat score.

        Args:
            agent_id: The assessing agent.
            target_entity_id: The target entity to assess.

        Returns:
            Threat score in [0.0, 1.0].
        """
        _time_module.sleep(0.001)
        memory = self._memories.get(agent_id, {})
        mem = memory.get(target_entity_id)
        if mem is None:
            return 0.0

        base_threat = mem.threat_level
        confidence = mem.detection_confidence

        # Velocity toward agent: higher threat if approaching
        entity = self._world_entities.get(target_entity_id, {})
        epos = entity.get("position", mem.last_position)
        evel = entity.get("velocity", mem.last_velocity)
        config = self._configs.get(agent_id)

        approach_factor = 0.5
        if config:
            # Get agent's position from first memory entry
            agent_pos = (0.0, 0.0, 0.0)
            dx = agent_pos[0] - epos[0]
            dz = agent_pos[2] - epos[2]
            dist = math.sqrt(dx * dx + dz * dz)
            if dist > 1e-8 and (evel[0] != 0 or evel[2] != 0):
                toward_x = -dx / dist
                toward_z = -dz / dist
                vel_mag = math.sqrt(evel[0] * evel[0] + evel[2] * evel[2])
                if vel_mag > 1e-8:
                    vel_norm_x = evel[0] / vel_mag
                    vel_norm_z = evel[2] / vel_mag
                    dot = vel_norm_x * toward_x + vel_norm_z * toward_z
                    approach_factor = 0.5 + 0.5 * max(0.0, dot)

        threat = base_threat * confidence * approach_factor
        return min(1.0, max(0.0, threat))

    def compute_interest(
        self,
        agent_id: str,
        target_entity_id: str,
    ) -> float:
        """Compute the interest level of a target entity for an agent.

        Factors include novelty, proximity, relevance to agent goals,
        and entity type match.

        Args:
            agent_id: The assessing agent.
            target_entity_id: The target entity.

        Returns:
            Interest score in [0.0, 1.0].
        """
        _time_module.sleep(0.001)
        memory = self._memories.get(agent_id, {})
        mem = memory.get(target_entity_id)
        if mem is None:
            return 0.0

        config = self._configs.get(agent_id)
        interest = mem.interest_level

        # Proximity bonus
        if config and config.vision_range > 0:
            entity = self._world_entities.get(target_entity_id, {})
            epos = entity.get("position", mem.last_position)
            dist = self._distance_3d((0.0, 0.0, 0.0), epos)  # approximate
            proximity = max(0.0, 1.0 - dist / config.vision_range)
            interest += proximity * 0.2

        # Novelty decays with more sightings
        novelty = max(0.0, 1.0 - mem.times_spotted * 0.1)
        interest += novelty * 0.2

        return min(1.0, max(0.0, interest))

    # ------------------------------------------------------------------
    # Memory & Events
    # ------------------------------------------------------------------

    def get_memory(self, agent_id: str) -> List[PerceptionMemory]:
        """Get all current memory entries for an agent."""
        return list(self._memories.get(agent_id, {}).values())

    def get_memory_entry(
        self,
        agent_id: str,
        entity_id: str,
    ) -> Optional[PerceptionMemory]:
        """Get a specific memory entry."""
        return self._memories.get(agent_id, {}).get(entity_id)

    def consume_events(self, agent_id: str) -> List[PerceptionEvent]:
        """Get and clear the event queue for an agent."""
        events = self._event_queues.pop(agent_id, [])
        self._event_queues[agent_id] = []
        return events

    def get_event_queue(self, agent_id: str) -> List[PerceptionEvent]:
        """Get the current event queue without clearing."""
        return list(self._event_queues.get(agent_id, []))

    def clear_memory(self, agent_id: str) -> None:
        """Clear all memory entries for an agent."""
        self._memories.pop(agent_id, None)
        self._memories[agent_id] = {}

    # ------------------------------------------------------------------
    # Query
    # ------------------------------------------------------------------

    def get_config(self, agent_id: str) -> Optional[PerceptionConfig]:
        """Get perception configuration for an agent."""
        return self._configs.get(agent_id)

    def get_stats(self) -> Dict[str, Any]:
        """Get perception system statistics."""
        total_memories = sum(len(m) for m in self._memories.values())
        total_events = sum(len(e) for e in self._event_queues.values())
        return {
            "total_agents_registered": self._total_agents_registered,
            "active_agents": len(self._configs),
            "total_world_entities": len(self._world_entities),
            "total_memory_entries": total_memories,
            "queued_events": total_events,
            "total_events_generated": self._total_events_generated,
        }

    def reset(self) -> None:
        """Reset all perception data."""
        self._configs.clear()
        self._memories.clear()
        self._event_queues.clear()
        self._world_entities.clear()
        self._total_events_generated = 0
        self._total_agents_registered = 0

    # ------------------------------------------------------------------
    # Internal Helpers
    # ------------------------------------------------------------------

    @staticmethod
    def _distance_3d(
        a: Tuple[float, float, float],
        b: Tuple[float, float, float],
    ) -> float:
        dx = a[0] - b[0]
        dy = a[1] - b[1]
        dz = a[2] - b[2]
        return math.sqrt(dx * dx + dy * dy + dz * dz)


