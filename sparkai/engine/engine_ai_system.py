class NavMeshGenerator:
    """Navigation mesh generation and optimization engine.

    Constructs polygon-based navigation meshes from input geometry,
    classifies area types, generates off-mesh links, and performs
    mesh simplification for efficient pathfinding queries.

    Usage:
        gen = NavMeshGenerator()
        config = NavMeshBakeConfig(agent_radius=0.5, agent_height=2.0)
        mesh = gen.build_navmesh("level1", geometry, config)
        gen.add_link(mesh.id, 0, 5, "jump", bidirectional=True)
        gen.optimize(mesh.id)
    """

    def __init__(self) -> None:
        self._meshes: Dict[str, NavMeshData] = {}
        self._total_meshes_built: int = 0
        self._total_links_created: int = 0

    # ------------------------------------------------------------------
    # NavMesh Construction
    # ------------------------------------------------------------------

    def build_navmesh(
        self,
        name: str,
        geometry: NavMeshGeometry,
        config: Optional[NavMeshBakeConfig] = None,
    ) -> NavMeshData:
        """Build a complete navigation mesh from input geometry.

        Processes triangle data, classifies walkable surfaces, merges
        contiguous regions, and generates a polygon graph.

        Args:
            name: Identifier for the navmesh.
            geometry: Input geometry with vertices and triangles.
            config: Optional bake configuration; defaults are used if None.

        Returns:
            The constructed NavMeshData.
        """
        _time_module.sleep(0.001)
        if config is None:
            config = NavMeshBakeConfig()

        mesh = NavMeshData(
            name=name,
            vertices=list(geometry.vertices),
            bounds=geometry.bounds,
            bake_config=config,
        )

        # Build polygons from triangles
        polygons, adjacency = self._build_polygon_graph(
            geometry, config,
        )
        mesh.polygons = polygons
        mesh.polygon_count = len(polygons)
        mesh.vertex_count = len(geometry.vertices)

        self._meshes[mesh.id] = mesh
        self._total_meshes_built += 1
        return mesh

    def _build_polygon_graph(
        self,
        geometry: NavMeshGeometry,
        config: NavMeshBakeConfig,
    ) -> Tuple[List[NavMeshPolygon], Dict[int, List[int]]]:
        """Internal: construct polygon graph from triangle soup.

        Each triangle becomes a polygon. Adjacency is determined by
        shared edges. Area types are assigned from geometry data.

        Returns:
            Tuple of (polygons, adjacency_dict).
        """
        polygons: List[NavMeshPolygon] = []
        adjacency: Dict[int, List[int]] = {}

        for i, (i0, i1, i2) in enumerate(geometry.triangles):
            if i0 >= len(geometry.vertices) or i1 >= len(geometry.vertices) or i2 >= len(geometry.vertices):
                continue
            v0 = geometry.vertices[i0]
            v1 = geometry.vertices[i1]
            v2 = geometry.vertices[i2]
            cx = (v0[0] + v1[0] + v2[0]) / 3.0
            cy = (v0[1] + v1[1] + v2[1]) / 3.0
            cz = (v0[2] + v1[2] + v2[2]) / 3.0
            area_type = (
                geometry.triangle_areas[i]
                if i < len(geometry.triangle_areas)
                else NavMeshAreaType.WALK.value
            )
            poly = NavMeshPolygon(
                id=i,
                vertex_indices=[i0, i1, i2],
                center=(cx, cy, cz),
                area_type=area_type,
                area_cost=1.0 if area_type == NavMeshAreaType.WALK.value else 2.0,
                neighbors=[],
                bounds_min=(
                    min(v0[0], v1[0], v2[0]),
                    min(v0[1], v1[1], v2[1]),
                    min(v0[2], v1[2], v2[2]),
                ),
                bounds_max=(
                    max(v0[0], v1[0], v2[0]),
                    max(v0[1], v1[1], v2[1]),
                    max(v0[2], v1[2], v2[2]),
                ),
            )
            polygons.append(poly)
            adjacency[i] = []

        # Build adjacency via shared edges
        edge_to_poly: Dict[Tuple[int, int], List[int]] = {}
        for poly in polygons:
            vi = poly.vertex_indices
            edges = [
                (min(vi[0], vi[1]), max(vi[0], vi[1])),
                (min(vi[1], vi[2]), max(vi[1], vi[2])),
                (min(vi[2], vi[0]), max(vi[2], vi[0])),
            ]
            for edge in edges:
                edge_to_poly.setdefault(edge, []).append(poly.id)

        for edge, poly_ids in edge_to_poly.items():
            if len(poly_ids) == 2:
                a, b = poly_ids[0], poly_ids[1]
                if b not in adjacency[a]:
                    adjacency[a].append(b)
                if a not in adjacency[b]:
                    adjacency[b].append(a)

        for poly_id, neighbors in adjacency.items():
            if poly_id < len(polygons):
                polygons[poly_id].neighbors = neighbors

        return polygons, adjacency

    def build_navmesh_from_grid(
        self,
        name: str,
        grid: List[List[int]],
        grid_width: int,
        grid_height: int,
        cell_size: float = 1.0,
        origin: Tuple[float, float, float] = (0.0, 0.0, 0.0),
    ) -> NavMeshData:
        """Build a navmesh from a 2D walkability grid.

        Each cell where grid[y][x] == 0 is walkable. Triangles are
        generated by splitting each walkable cell into two triangles.

        Args:
            name: Identifier for the navmesh.
            grid: 2D grid where 0 = walkable, non-zero = blocked.
            grid_width: Number of columns in the grid.
            grid_height: Number of rows in the grid.
            cell_size: World-space size of each grid cell.
            origin: World-space origin of the grid bottom-left corner.

        Returns:
            The constructed NavMeshData.
        """
        _time_module.sleep(0.001)
        ox, oy, oz = origin
        vertices: List[Tuple[float, float, float]] = []
        triangles: List[Tuple[int, int, int]] = []
        triangle_areas: List[str] = []
        vertex_map: Dict[Tuple[int, int], int] = {}

        def get_or_create_vertex(gx: int, gy: int) -> int:
            key = (gx, gy)
            if key not in vertex_map:
                wx = ox + gx * cell_size
                wz = oz + (grid_height - gy) * cell_size
                wy = oy
                vertex_map[key] = len(vertices)
                vertices.append((wx, wy, wz))
            return vertex_map[key]

        for gy in range(grid_height):
            for gx in range(grid_width):
                if gy >= len(grid) or gx >= len(grid[gy]):
                    continue
                if grid[gy][gx] != 0:
                    continue
                v00 = get_or_create_vertex(gx, gy)
                v10 = get_or_create_vertex(gx + 1, gy)
                v01 = get_or_create_vertex(gx, gy + 1)
                v11 = get_or_create_vertex(gx + 1, gy + 1)
                triangles.append((v00, v10, v11))
                triangles.append((v00, v11, v01))
                triangle_areas.append(NavMeshAreaType.WALK.value)
                triangle_areas.append(NavMeshAreaType.WALK.value)

        min_x = ox
        max_x = ox + grid_width * cell_size
        min_z = oz
        max_z = oz + grid_height * cell_size
        geometry = NavMeshGeometry(
            vertices=vertices,
            triangles=triangles,
            triangle_areas=triangle_areas,
            bounds=(min_x, oy, min_z, max_x, oy, max_z),
            name=name,
        )
        return self.build_navmesh(name, geometry)

    # ------------------------------------------------------------------
    # Off-Mesh Links
    # ------------------------------------------------------------------

    def add_link(
        self,
        mesh_id: str,
        from_polygon: int,
        to_polygon: int,
        link_type: str = NavMeshAreaType.WALK.value,
        bidirectional: bool = True,
        cost_multiplier: float = 1.0,
        start_point: Optional[Tuple[float, float, float]] = None,
        end_point: Optional[Tuple[float, float, float]] = None,
    ) -> Optional[NavMeshLink]:
        """Add an off-mesh link between two polygons.

        Args:
            mesh_id: Target navmesh identifier.
            from_polygon: Source polygon index.
            to_polygon: Destination polygon index.
            link_type: Traversal type (walk, jump, swim, fly).
            bidirectional: Whether the link works both ways.
            cost_multiplier: Path cost multiplier for this link.
            start_point: Optional world-space start point.
            end_point: Optional world-space end point.

        Returns:
            The created NavMeshLink, or None if the mesh is not found.
        """
        _time_module.sleep(0.001)
        mesh = self._meshes.get(mesh_id)
        if mesh is None:
            return None
        if from_polygon >= len(mesh.polygons) or to_polygon >= len(mesh.polygons):
            return None

        sp = start_point or mesh.polygons[from_polygon].center
        ep = end_point or mesh.polygons[to_polygon].center

        link = NavMeshLink(
            from_polygon=from_polygon,
            to_polygon=to_polygon,
            link_type=link_type,
            bidirectional=bidirectional,
            cost_multiplier=cost_multiplier,
            start_point=sp,
            end_point=ep,
        )
        mesh.links.append(link)

        # Register adjacency
        if to_polygon not in mesh.polygons[from_polygon].neighbors:
            mesh.polygons[from_polygon].neighbors.append(to_polygon)
        if bidirectional and from_polygon not in mesh.polygons[to_polygon].neighbors:
            mesh.polygons[to_polygon].neighbors.append(from_polygon)

        mesh.link_count = len(mesh.links)
        self._total_links_created += 1
        return link

    # ------------------------------------------------------------------
    # Optimization
    # ------------------------------------------------------------------

    def optimize(self, mesh_id: str) -> Dict[str, Any]:
        """Optimize a navigation mesh by merging adjacent same-area polygons.

        Walks the polygon graph and merges neighboring polygons that
        share the same area type, reducing node count for pathfinding.

        Args:
            mesh_id: Target navmesh identifier.

        Returns:
            Dict with optimization statistics.
        """
        _time_module.sleep(0.001)
        mesh = self._meshes.get(mesh_id)
        if mesh is None:
            return {"error": f"NavMesh '{mesh_id}' not found"}

        original_count = len(mesh.polygons)
        merged: int = 0

        # Simple merge: combine adjacent polygons with same area type
        merged_set: Set[int] = set()
        new_polygons: List[NavMeshPolygon] = []
        merge_map: Dict[int, int] = {}

        for i, poly in enumerate(mesh.polygons):
            if i in merged_set:
                continue
            merged_set.add(i)
            combined_verts = list(poly.vertex_indices)
            combined_neighbors = list(poly.neighbors)
            for j in poly.neighbors:
                if j in merged_set or j >= len(mesh.polygons):
                    continue
                neighbor = mesh.polygons[j]
                if neighbor.area_type == poly.area_type:
                    merged_set.add(j)
                    for vi in neighbor.vertex_indices:
                        if vi not in combined_verts:
                            combined_verts.append(vi)
                    for nj in neighbor.neighbors:
                        if nj != i and nj not in combined_neighbors:
                            combined_neighbors.append(nj)
                    merged += 1

            new_id = len(new_polygons)
            merge_map[i] = new_id
            new_poly = NavMeshPolygon(
                id=new_id,
                vertex_indices=combined_verts,
                center=poly.center,
                area_type=poly.area_type,
                area_cost=poly.area_cost,
                neighbors=combined_neighbors,
                bounds_min=poly.bounds_min,
                bounds_max=poly.bounds_max,
            )
            new_polygons.append(new_poly)

        # Remap neighbor indices
        for poly in new_polygons:
            remapped = []
            for nb in poly.neighbors:
                # Find which new polygon contains this neighbor
                for old_idx, new_idx in merge_map.items():
                    if old_idx == nb:
                        if new_idx != poly.id and new_idx not in remapped:
                            remapped.append(new_idx)
                        break
            poly.neighbors = remapped

        mesh.polygons = new_polygons
        mesh.polygon_count = len(new_polygons)

        return {
            "original_polygons": original_count,
            "optimized_polygons": len(new_polygons),
            "polygons_merged": merged,
            "reduction_percent": round(
                (1 - len(new_polygons) / max(1, original_count)) * 100, 1
            ),
        }

    def simplify(self, mesh_id: str, max_error: float = 1.0) -> Dict[str, Any]:
        """Simplify navmesh geometry by removing near-duplicate vertices.

        Merges vertices within max_error distance of each other to
        reduce vertex count while preserving polygon topology.

        Args:
            mesh_id: Target navmesh identifier.
            max_error: Maximum merge distance between vertices.

        Returns:
            Dict with simplification statistics.
        """
        _time_module.sleep(0.001)
        mesh = self._meshes.get(mesh_id)
        if mesh is None:
            return {"error": f"NavMesh '{mesh_id}' not found"}

        original_verts = len(mesh.vertices)
        if original_verts <= 1:
            return {"original_vertices": original_verts, "simplified_vertices": original_verts, "removed": 0}

        # Merge close vertices
        new_vertices: List[Tuple[float, float, float]] = []
        remap: Dict[int, int] = {}
        max_error_sq = max_error * max_error

        for i, v in enumerate(mesh.vertices):
            best_idx = -1
            best_dist = max_error_sq
            for j, nv in enumerate(new_vertices):
                dx = v[0] - nv[0]
                dy = v[1] - nv[1]
                dz = v[2] - nv[2]
                d2 = dx * dx + dy * dy + dz * dz
                if d2 < best_dist:
                    best_dist = d2
                    best_idx = j
            if best_idx >= 0:
                remap[i] = best_idx
            else:
                remap[i] = len(new_vertices)
                new_vertices.append(v)

        mesh.vertices = new_vertices
        mesh.vertex_count = len(new_vertices)

        # Remap polygon vertex indices
        for poly in mesh.polygons:
            poly.vertex_indices = [remap.get(vi, vi) for vi in poly.vertex_indices]

        removed = original_verts - len(new_vertices)
        return {
            "original_vertices": original_verts,
            "simplified_vertices": len(new_vertices),
            "removed": removed,
        }

    # ------------------------------------------------------------------
    # Query
    # ------------------------------------------------------------------

    def get_mesh(self, mesh_id: str) -> Optional[NavMeshData]:
        """Get a navmesh by ID."""
        return self._meshes.get(mesh_id)

    def list_meshes(self) -> List[NavMeshData]:
        """List all navmeshes."""
        return list(self._meshes.values())

    def remove_mesh(self, mesh_id: str) -> bool:
        """Remove a navmesh by ID."""
        if mesh_id in self._meshes:
            del self._meshes[mesh_id]
            return True
        return False

    def get_stats(self) -> Dict[str, Any]:
        """Get generator statistics."""
        total_polys = sum(m.polygon_count for m in self._meshes.values())
        total_verts = sum(m.vertex_count for m in self._meshes.values())
        return {
            "total_meshes": len(self._meshes),
            "total_meshes_built": self._total_meshes_built,
            "total_links_created": self._total_links_created,
            "total_polygons": total_polys,
            "total_vertices": total_verts,
        }

    def reset(self) -> None:
        """Clear all navmesh data."""
        self._meshes.clear()
        self._total_meshes_built = 0
        self._total_links_created = 0


