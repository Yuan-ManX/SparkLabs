class BehaviorTreeRuntime:
    """Runtime execution engine for behavior trees.

    Manages tree lifecycle, node ticking, blackboard state, decorator
    evaluation, tree serialization, and visualization data export.

    Usage:
        bt = BehaviorTreeRuntime()
        tree_id = bt.create_tree("patrol")
        seq = bt.add_node(tree_id, "root", BehaviorNodeKind.SEQUENCE.value)
        cond = bt.add_node(tree_id, "see_enemy", BehaviorNodeKind.CONDITION.value, parent_id=seq)
        act = bt.add_node(tree_id, "chase", BehaviorNodeKind.ACTION.value, parent_id=seq)
        bt.set_root(tree_id, seq)
        result = bt.tick_tree(tree_id)
    """

    def __init__(self) -> None:
        self._trees: Dict[str, BTTree] = {}
        self._nodes: Dict[str, BTNode] = {}
        self._tree_nodes: Dict[str, List[str]] = {}
        self._total_trees_created: int = 0
        self._total_nodes_created: int = 0
        self._total_ticks: int = 0

    # ------------------------------------------------------------------
    # Tree Lifecycle
    # ------------------------------------------------------------------

    def create_tree(
        self,
        name: str,
        tick_rate: float = 0.1,
    ) -> str:
        """Create a new behavior tree.

        Args:
            name: Display name for the tree.
            tick_rate: Tick interval in seconds.

        Returns:
            The tree ID.
        """
        _time_module.sleep(0.001)
        tree = BTTree(name=name, tick_rate=tick_rate)
        tree.blackboard.tree_id = tree.id
        self._trees[tree.id] = tree
        self._tree_nodes[tree.id] = []
        self._total_trees_created += 1
        return tree.id

    def add_node(
        self,
        tree_id: str,
        name: str,
        node_kind: str = BehaviorNodeKind.ACTION.value,
        parent_id: str = "",
        decorator_kind: str = "",
        condition_name: str = "",
        action_name: str = "",
        params: Optional[Dict[str, Any]] = None,
        priority: int = 0,
        cooldown_seconds: float = 0.0,
        timeout_seconds: float = 0.0,
        repeat_count: int = 0,
        tags: Optional[List[str]] = None,
    ) -> str:
        """Add a node to a behavior tree.

        Args:
            tree_id: Target tree identifier.
            name: Node display name.
            node_kind: Node type (sequence, selector, parallel, condition, action, decorator).
            parent_id: Parent node ID (empty for root-level nodes).
            decorator_kind: Decorator type if node_kind is decorator.
            condition_name: Condition identifier for condition nodes.
            action_name: Action identifier for action nodes.
            params: Optional parameters dict.
            priority: Execution priority (higher = earlier).
            cooldown_seconds: Minimum seconds between executions.
            timeout_seconds: Maximum execution duration.
            repeat_count: Number of repetitions for repeater decorators.
            tags: Optional tag list.

        Returns:
            The node ID.
        """
        _time_module.sleep(0.001)
        tree = self._trees.get(tree_id)
        if tree is None:
            return ""

        node = BTNode(
            node_kind=node_kind,
            name=name,
            parent_id=parent_id,
            decorator_kind=decorator_kind,
            condition_name=condition_name,
            action_name=action_name,
            params=params or {},
            priority=priority,
            cooldown_seconds=cooldown_seconds,
            timeout_seconds=timeout_seconds,
            repeat_count=repeat_count,
            tags=tags or [],
        )
        self._nodes[node.id] = node
        self._tree_nodes.setdefault(tree_id, []).append(node.id)

        # Link to parent
        if parent_id and parent_id in self._nodes:
            parent = self._nodes[parent_id]
            parent.children_ids.append(node.id)
            # Sort children by priority
            parent.children_ids.sort(
                key=lambda cid: self._nodes[cid].priority if cid in self._nodes else 0,
                reverse=True,
            )

        self._total_nodes_created += 1
        return node.id

    def set_root(self, tree_id: str, node_id: str) -> bool:
        """Set the root node of a behavior tree.

        Args:
            tree_id: Target tree identifier.
            node_id: Node ID to set as root.

        Returns:
            True if successful.
        """
        tree = self._trees.get(tree_id)
        if tree is None or node_id not in self._nodes:
            return False
        tree.root_node_id = node_id
        return True

    def remove_node(self, node_id: str) -> bool:
        """Remove a node and all its children from the tree."""
        if node_id not in self._nodes:
            return False
        # Collect subtree
        to_remove = self._collect_subtree(node_id)
        for nid in to_remove:
            # Remove from parent's children
            node = self._nodes.get(nid)
            if node and node.parent_id and node.parent_id in self._nodes:
                parent = self._nodes[node.parent_id]
                if nid in parent.children_ids:
                    parent.children_ids.remove(nid)
            self._nodes.pop(nid, None)
        return True

    def _collect_subtree(self, root_id: str) -> List[str]:
        """Collect all node IDs in a subtree."""
        result: List[str] = []
        stack = [root_id]
        while stack:
            nid = stack.pop()
            if nid in result:
                continue
            result.append(nid)
            node = self._nodes.get(nid)
            if node:
                stack.extend(node.children_ids)
        return result

    # ------------------------------------------------------------------
    # Tree Execution
    # ------------------------------------------------------------------

    def tick_tree(self, tree_id: str) -> BehaviorResult:
        """Execute one tick of a behavior tree.

        Traverses the tree from the root, evaluating composite nodes,
        decorators, conditions, and actions. Stores results on nodes.

        Args:
            tree_id: The tree to tick.

        Returns:
            The overall tree result (SUCCESS, FAILURE, or RUNNING).
        """
        _time_module.sleep(0.001)
        tree = self._trees.get(tree_id)
        if tree is None or not tree.root_node_id:
            return BehaviorResult.FAILURE

        tree.is_running = True
        tree.total_ticks += 1
        self._total_ticks += 1

        result = self._tick_node(tree.root_node_id, tree)
        tree.is_running = (result == BehaviorResult.RUNNING)

        if result == BehaviorResult.SUCCESS:
            tree.total_successes += 1
        elif result == BehaviorResult.FAILURE:
            tree.total_failures += 1

        return result

    def _tick_node(self, node_id: str, tree: BTTree) -> BehaviorResult:
        """Tick a single node, dispatching by node kind."""
        node = self._nodes.get(node_id)
        if node is None:
            return BehaviorResult.FAILURE

        now = _time_module.time()

        # Cooldown check
        if node.cooldown_seconds > 0:
            if now - node.last_tick_time < node.cooldown_seconds and node.last_tick_time > 0:
                return BehaviorResult.FAILURE

        node.last_tick_time = now
        node.execution_count += 1

        kind = node.node_kind

        if kind == BehaviorNodeKind.SEQUENCE.value:
            result = self._tick_sequence(node, tree)
        elif kind == BehaviorNodeKind.SELECTOR.value:
            result = self._tick_selector(node, tree)
        elif kind == BehaviorNodeKind.PARALLEL.value:
            result = self._tick_parallel(node, tree)
        elif kind == BehaviorNodeKind.CONDITION.value:
            result = self._tick_condition(node, tree)
        elif kind == BehaviorNodeKind.ACTION.value:
            result = self._tick_action(node, tree)
        elif kind == BehaviorNodeKind.DECORATOR.value:
            result = self._tick_decorator(node, tree)
        else:
            result = BehaviorResult.FAILURE

        node.last_result = result.value
        return result

    def _tick_sequence(self, node: BTNode, tree: BTTree) -> BehaviorResult:
        for child_id in node.children_ids:
            result = self._tick_node(child_id, tree)
            if result != BehaviorResult.SUCCESS:
                return result
        return BehaviorResult.SUCCESS

    def _tick_selector(self, node: BTNode, tree: BTTree) -> BehaviorResult:
        for child_id in node.children_ids:
            result = self._tick_node(child_id, tree)
            if result != BehaviorResult.FAILURE:
                return result
        return BehaviorResult.FAILURE

    def _tick_parallel(self, node: BTNode, tree: BTTree) -> BehaviorResult:
        success_count = 0
        failure_count = 0
        for child_id in node.children_ids:
            result = self._tick_node(child_id, tree)
            if result == BehaviorResult.SUCCESS:
                success_count += 1
            elif result == BehaviorResult.FAILURE:
                failure_count += 1
        required = node.params.get("required_successes", len(node.children_ids))
        if success_count >= required:
            return BehaviorResult.SUCCESS
        if failure_count > len(node.children_ids) - required:
            return BehaviorResult.FAILURE
        return BehaviorResult.RUNNING

    def _tick_condition(self, node: BTNode, tree: BTTree) -> BehaviorResult:
        result = self._evaluate_condition(node, tree)
        return BehaviorResult.SUCCESS if result else BehaviorResult.FAILURE

    def _tick_action(self, node: BTNode, tree: BTTree) -> BehaviorResult:
        return self._execute_action(node, tree)

    def _tick_decorator(self, node: BTNode, tree: BTTree) -> BehaviorResult:
        if not node.children_ids:
            return BehaviorResult.FAILURE
        child_id = node.children_ids[0]
        dk = node.decorator_kind

        if dk == DecoratorKind.INVERTER.value:
            result = self._tick_node(child_id, tree)
            if result == BehaviorResult.SUCCESS:
                return BehaviorResult.FAILURE
            if result == BehaviorResult.FAILURE:
                return BehaviorResult.SUCCESS
            return result

        if dk == DecoratorKind.REPEATER.value:
            count = node.repeat_count
            if count <= 0:
                count = node.params.get("repeat_count", 1)
            for _ in range(count):
                result = self._tick_node(child_id, tree)
                if result == BehaviorResult.FAILURE:
                    return BehaviorResult.FAILURE
            return BehaviorResult.SUCCESS

        if dk == DecoratorKind.UNTIL_FAILURE.value:
            while True:
                result = self._tick_node(child_id, tree)
                if result == BehaviorResult.FAILURE:
                    return BehaviorResult.SUCCESS
                if result == BehaviorResult.RUNNING:
                    return BehaviorResult.RUNNING
            return BehaviorResult.SUCCESS

        if dk == DecoratorKind.TIMER.value:
            start_key = f"_timer_start_{node.id}"
            duration = node.timeout_seconds
            if duration <= 0:
                duration = node.params.get("duration", 1.0)
            if not tree.blackboard.has(start_key):
                tree.blackboard.set(start_key, _time_module.time())
            elapsed = _time_module.time() - tree.blackboard.get(start_key, 0.0)
            if elapsed >= duration:
                tree.blackboard.delete(start_key)
                return BehaviorResult.SUCCESS
            result = self._tick_node(child_id, tree)
            return result

        if dk == DecoratorKind.COOLDOWN.value:
            last_key = f"_cooldown_last_{node.id}"
            cooldown = node.cooldown_seconds
            if cooldown <= 0:
                cooldown = node.params.get("cooldown", 1.0)
            last_time = tree.blackboard.get(last_key, 0.0)
            if _time_module.time() - last_time < cooldown:
                return BehaviorResult.FAILURE
            result = self._tick_node(child_id, tree)
            if result == BehaviorResult.SUCCESS:
                tree.blackboard.set(last_key, _time_module.time())
            return result

        # Default: pass through
        return self._tick_node(child_id, tree)

    # ------------------------------------------------------------------
    # Condition / Action Resolution
    # ------------------------------------------------------------------

    def _evaluate_condition(self, node: BTNode, tree: BTTree) -> bool:
        """Evaluate a condition node against the blackboard."""
        name = node.condition_name
        bb = tree.blackboard

        if name == "blackboard_has":
            key = node.params.get("key", "")
            return bb.has(key)
        if name == "blackboard_equals":
            key = node.params.get("key", "")
            expected = node.params.get("value")
            return bb.get(key) == expected
        if name == "blackboard_greater":
            key = node.params.get("key", "")
            threshold = node.params.get("threshold", 0)
            val = bb.get(key, 0)
            try:
                return float(val) > float(threshold)
            except (ValueError, TypeError):
                return False
        if name == "has_signal":
            signal = node.params.get("signal", "")
            return signal in bb.signals
        if name == "timer_expired":
            key = node.params.get("timer_key", "")
            return bb.is_timer_expired(key)
        if name == "probability":
            prob = node.params.get("probability", 0.5)
            return random.random() < prob
        if name == "always_true":
            return True
        if name == "always_false":
            return False
        # Default: true for unknown conditions
        return True

    def _execute_action(self, node: BTNode, tree: BTTree) -> BehaviorResult:
        """Execute an action node against the blackboard."""
        name = node.action_name
        bb = tree.blackboard

        if name == "set_blackboard":
            key = node.params.get("key", "")
            value = node.params.get("value")
            if key:
                bb.set(key, value)
            return BehaviorResult.SUCCESS
        if name == "delete_blackboard":
            key = node.params.get("key", "")
            bb.delete(key)
            return BehaviorResult.SUCCESS
        if name == "emit_signal":
            signal = node.params.get("signal", "")
            if signal:
                bb.emit_signal(signal)
            return BehaviorResult.SUCCESS
        if name == "set_timer":
            key = node.params.get("timer_key", "")
            duration = node.params.get("duration", 1.0)
            if key:
                bb.set_timer(key, duration)
            return BehaviorResult.SUCCESS
        if name == "wait":
            return BehaviorResult.RUNNING
        if name == "succeed":
            return BehaviorResult.SUCCESS
        if name == "fail":
            return BehaviorResult.FAILURE
        if name == "random_succeed":
            if random.random() < node.params.get("probability", 0.5):
                return BehaviorResult.SUCCESS
            return BehaviorResult.FAILURE
        # Default: succeed for unknown actions
        return BehaviorResult.SUCCESS

    # ------------------------------------------------------------------
    # Serialization
    # ------------------------------------------------------------------

    def serialize_tree(self, tree_id: str) -> str:
        """Serialize a behavior tree to a JSON string.

        Args:
            tree_id: The tree to serialize.

        Returns:
            A JSON string representation of the tree.
        """
        import json

        tree = self._trees.get(tree_id)
        if tree is None:
            return "{}"

        node_ids = self._tree_nodes.get(tree_id, [])
        data = {
            "tree": tree.to_dict(),
            "nodes": [self._nodes[nid].to_dict() for nid in node_ids if nid in self._nodes],
        }
        return json.dumps(data, indent=2)

    def deserialize_tree(self, json_str: str) -> str:
        """Deserialize a behavior tree from a JSON string.

        Args:
            json_str: JSON string produced by serialize_tree.

        Returns:
            The new tree ID.
        """
        import json

        try:
            data = json.loads(json_str)
        except (json.JSONDecodeError, TypeError):
            return ""

        tree_data = data.get("tree", {})
        nodes_data = data.get("nodes", [])

        tree = BTTree(
            name=tree_data.get("name", "Imported Tree"),
            tick_rate=tree_data.get("tick_rate", 0.1),
        )
        tree.blackboard.tree_id = tree.id
        tree.blackboard.entries = tree_data.get("blackboard", {}).get("entries", {})
        self._trees[tree.id] = tree
        self._tree_nodes[tree.id] = []

        old_to_new: Dict[str, str] = {}
        for node_data in nodes_data:
            old_id = node_data.get("id", "")
            node = BTNode(
                node_kind=node_data.get("node_kind", BehaviorNodeKind.ACTION.value),
                name=node_data.get("name", ""),
                decorator_kind=node_data.get("decorator_kind", ""),
                condition_name=node_data.get("condition_name", ""),
                action_name=node_data.get("action_name", ""),
                params=node_data.get("params", {}),
                priority=node_data.get("priority", 0),
                cooldown_seconds=node_data.get("cooldown_seconds", 0.0),
                timeout_seconds=node_data.get("timeout_seconds", 0.0),
                repeat_count=node_data.get("repeat_count", 0),
                tags=node_data.get("tags", []),
            )
            self._nodes[node.id] = node
            self._tree_nodes[tree.id].append(node.id)
            old_to_new[old_id] = node.id

        # Rebuild parent/child relationships
        for node_data in nodes_data:
            old_id = node_data.get("id", "")
            new_id = old_to_new.get(old_id)
            if new_id and new_id in self._nodes:
                node = self._nodes[new_id]
                old_parent = node_data.get("parent_id", "")
                node.parent_id = old_to_new.get(old_parent, "")
                for old_child in node_data.get("children_ids", []):
                    new_child = old_to_new.get(old_child)
                    if new_child:
                        node.children_ids.append(new_child)

        tree.root_node_id = old_to_new.get(tree_data.get("root_node_id", ""), "")
        self._total_trees_created += 1
        return tree.id

    # ------------------------------------------------------------------
    # Visualization Export
    # ------------------------------------------------------------------

    def export_visualization_data(self, tree_id: str) -> Dict[str, Any]:
        """Export tree data for visualization (node graph editor, debugger).

        Returns a dict with nodes and edges suitable for rendering
        a behavior tree graph in a UI tool or debug overlay.

        Args:
            tree_id: The tree to visualize.

        Returns:
            Dict with 'nodes' and 'edges' lists.
        """
        _time_module.sleep(0.001)
        tree = self._trees.get(tree_id)
        if tree is None:
            return {"nodes": [], "edges": []}

        node_ids = self._tree_nodes.get(tree_id, [])
        vis_nodes = []
        vis_edges = []

        for nid in node_ids:
            node = self._nodes.get(nid)
            if node is None:
                continue
            vis_nodes.append({
                "id": node.id,
                "label": node.name,
                "kind": node.node_kind,
                "decorator": node.decorator_kind,
                "last_result": node.last_result,
                "execution_count": node.execution_count,
                "priority": node.priority,
                "is_root": node.id == tree.root_node_id,
            })
            for child_id in node.children_ids:
                vis_edges.append({
                    "from": node.id,
                    "to": child_id,
                })

        return {
            "tree_id": tree_id,
            "tree_name": tree.name,
            "nodes": vis_nodes,
            "edges": vis_edges,
        }

    def export_dot(self, tree_id: str) -> str:
        """Export tree as Graphviz DOT format string.

        Args:
            tree_id: The tree to export.

        Returns:
            A DOT format string for graph rendering.
        """
        tree = self._trees.get(tree_id)
        if tree is None:
            return "digraph {}"

        node_ids = self._tree_nodes.get(tree_id, [])
        lines = [f'digraph "{tree.name}" {{']
        lines.append(f'  label="{tree.name}";')
        lines.append('  node [shape=box, style=filled, fillcolor=lightyellow];')

        for nid in node_ids:
            node = self._nodes.get(nid)
            if node is None:
                continue
            result_color = {
                "success": "lightgreen",
                "failure": "lightcoral",
                "running": "lightblue",
            }.get(node.last_result, "lightyellow")
            label = f"{node.name}\\n[{node.node_kind}]"
            if node.decorator_kind:
                label += f"\\n<{node.decorator_kind}>"
            lines.append(
                f'  "{nid}" [label="{label}", fillcolor={result_color}];'
            )

        for nid in node_ids:
            node = self._nodes.get(nid)
            if node is None:
                continue
            for child_id in node.children_ids:
                lines.append(f'  "{nid}" -> "{child_id}";')

        lines.append("}")
        return "\n".join(lines)

    # ------------------------------------------------------------------
    # Query
    # ------------------------------------------------------------------

    def get_tree(self, tree_id: str) -> Optional[BTTree]:
        """Get a behavior tree by ID."""
        return self._trees.get(tree_id)

    def get_node(self, node_id: str) -> Optional[BTNode]:
        """Get a node by ID."""
        return self._nodes.get(node_id)

    def get_node_stats(self, node_id: str) -> Dict[str, Any]:
        """Get execution statistics for a node."""
        node = self._nodes.get(node_id)
        if node is None:
            return {}
        return {
            "execution_count": node.execution_count,
            "last_result": node.last_result,
            "last_tick_time": node.last_tick_time,
            "cooldown_seconds": node.cooldown_seconds,
            "timeout_seconds": node.timeout_seconds,
        }

    def list_trees(self) -> List[BTTree]:
        """List all behavior trees."""
        return list(self._trees.values())

    def remove_tree(self, tree_id: str) -> bool:
        """Remove a tree and all its nodes."""
        if tree_id not in self._trees:
            return False
        node_ids = self._tree_nodes.pop(tree_id, [])
        for nid in node_ids:
            self._nodes.pop(nid, None)
        del self._trees[tree_id]
        return True

    def get_stats(self) -> Dict[str, Any]:
        """Get runtime statistics."""
        active_trees = sum(1 for t in self._trees.values() if t.is_running)
        return {
            "total_trees": len(self._trees),
            "total_trees_created": self._total_trees_created,
            "total_nodes": len(self._nodes),
            "total_nodes_created": self._total_nodes_created,
            "total_ticks": self._total_ticks,
            "active_trees": active_trees,
        }

    def reset(self) -> None:
        """Reset all behavior tree data."""
        self._trees.clear()
        self._nodes.clear()
        self._tree_nodes.clear()
        self._total_trees_created = 0
        self._total_nodes_created = 0
        self._total_ticks = 0


