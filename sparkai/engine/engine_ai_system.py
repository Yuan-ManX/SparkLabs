    # Performance Budgeting
    # ------------------------------------------------------------------

    def set_global_budget(self, budget_ms: float) -> None:
        """Set the global AI CPU budget per frame.

        Args:
            budget_ms: Maximum milliseconds per frame for AI.
        """
        self.GLOBAL_AI_BUDGET_MS = max(0.1, budget_ms)

    def set_agent_budget(self, agent_id: str, budget_ms: float) -> bool:
        """Set a per-agent CPU budget.

        Args:
            agent_id: Target agent identifier.
            budget_ms: Maximum milliseconds per tick for this agent.

        Returns:
            True if the agent was found.
        """
        agent = self._agents.get(agent_id)
        if agent is None:
            return False
        agent.cpu_budget_ms = max(0.01, budget_ms)
        return True

    def get_performance_report(self) -> Dict[str, Any]:
        """Get a detailed AI performance report.

        Returns CPU usage breakdown by agent, average tick times,
        and budget utilization statistics.
        """
        total_agents = len(self._agents)
        if total_agents == 0:
            return {
                "total_agents": 0,
                "total_ticks": self._total_ticks,
                "total_cpu_ms": round(self._total_cpu_ms, 4),
                "avg_cpu_per_tick_ms": 0.0,
                "global_budget_ms": self.GLOBAL_AI_BUDGET_MS,
                "budget_utilization_percent": 0.0,
                "agent_breakdown": [],
            }

        avg_cpu = self._total_cpu_ms / max(1, self._total_ticks)
        budget_util = (avg_cpu / self.GLOBAL_AI_BUDGET_MS) * 100.0 if self.GLOBAL_AI_BUDGET_MS > 0 else 0.0

        agent_breakdown = []
        for agent in self._agents.values():
            agent_breakdown.append({
                "agent_id": agent.id,
                "name": agent.name,
                "state": agent.agent_state.value,
                "cpu_usage_ms": round(agent.cpu_usage_ms, 4),
                "cpu_budget_ms": agent.cpu_budget_ms,
                "tick_rate": agent.tick_rate,
                "last_tick": agent.last_tick_time,
            })

        return {
            "total_agents": total_agents,
            "total_ticks": self._total_ticks,
            "total_cpu_ms": round(self._total_cpu_ms, 4),
            "avg_cpu_per_tick_ms": round(avg_cpu, 4),
            "global_budget_ms": self.GLOBAL_AI_BUDGET_MS,
            "budget_utilization_percent": round(budget_util, 1),
            "agent_breakdown": agent_breakdown,
        }

