class StateMachineAI:
    """Finite state machine runtime for AI behavior.

    Manages state definitions with enter/update/exit callbacks,
    transition rules with conditions, nested sub-machines, global
    transitions, and state machine visualization.

    Usage:
        sm = StateMachineAI()
        machine_id = sm.create_state_machine("combat")
        idle = sm.add_state(machine_id, "idle", is_initial=True)
        attack = sm.add_state(machine_id, "attack")
        sm.add_transition(machine_id, "idle_to_attack", "idle", "attack", "enemy_spotted")
        sm.tick(machine_id)
    """

    def __init__(self) -> None:
        self._machines: Dict[str, AIStateMachine] = {}
        self._states: Dict[str, AIState] = {}
        self._transitions: Dict[str, AIStateTransition] = {}
        self._machine_states: Dict[str, List[str]] = {}
        self._machine_transitions: Dict[str, List[str]] = {}
        self._condition_handlers: Dict[str, Callable] = {}
        self._total_machines_created: int = 0
        self._total_states_created: int = 0
        self._total_transitions_created: int = 0

    # ------------------------------------------------------------------
    # State Machine Lifecycle
    # ------------------------------------------------------------------

    def create_state_machine(
        self,
        name: str,
        metadata: Optional[Dict[str, Any]] = None,
    ) -> str:
        """Create a new finite state machine.

        Args:
            name: Display name for the machine.
            metadata: Optional metadata dict.

        Returns:
            The machine ID.
        """
        _time_module.sleep(0.001)
        machine = AIStateMachine(
            name=name,
            metadata=metadata or {},
        )
        self._machines[machine.id] = machine
        self._machine_states[machine.id] = []
        self._machine_transitions[machine.id] = []
        self._total_machines_created += 1
        return machine.id

    def add_state(
        self,
        machine_id: str,
        name: str,
        is_initial: bool = False,
        sub_machine_id: str = "",
        enter_actions: Optional[List[str]] = None,
        update_actions: Optional[List[str]] = None,
        exit_actions: Optional[List[str]] = None,
        tags: Optional[List[str]] = None,
        metadata: Optional[Dict[str, Any]] = None,
    ) -> str:
        """Add a state to a state machine.

        Args:
            machine_id: Target machine identifier.
            name: State display name.
            is_initial: Whether this is the entry state.
            sub_machine_id: Optional nested sub-machine ID.
            enter_actions: Action names to run on enter.
            update_actions: Action names to run each tick.
            exit_actions: Action names to run on exit.
            tags: Optional tag list.
            metadata: Optional metadata dict.

        Returns:
            The state ID.
        """
        _time_module.sleep(0.001)
        if machine_id not in self._machines:
            return ""

        state = AIState(
            name=name,
            machine_id=machine_id,
            is_initial=is_initial,
            sub_machine_id=sub_machine_id,
            enter_actions=enter_actions or [],
            update_actions=update_actions or [],
            exit_actions=exit_actions or [],
            tags=tags or [],
            metadata=metadata or {},
        )
        self._states[state.id] = state
        self._machine_states.setdefault(machine_id, []).append(state.id)

        if is_initial:
            machine = self._machines[machine_id]
            machine.current_state_id = state.id

        self._total_states_created += 1
        return state.id

    def add_transition(
        self,
        machine_id: str,
        name: str,
        from_state_id: str,
        to_state_id: str,
        condition: str,
        priority: int = 0,
        is_global: bool = False,
        cooldown: float = 0.0,
        metadata: Optional[Dict[str, Any]] = None,
    ) -> str:
        """Add a transition between two states.

        Args:
            machine_id: Target machine identifier.
            name: Transition display name.
            from_state_id: Source state ID.
            to_state_id: Destination state ID.
            condition: Condition string to evaluate.
            priority: Evaluation priority (higher = checked first).
            is_global: Whether this transition applies from any state.
            cooldown: Minimum seconds between triggers.
            metadata: Optional metadata dict.

        Returns:
            The transition ID.
        """
        _time_module.sleep(0.001)
        if machine_id not in self._machines:
            return ""

        transition = AIStateTransition(
            name=name,
            machine_id=machine_id,
            from_state_id=from_state_id,
            to_state_id=to_state_id,
            condition=condition,
            priority=priority,
            is_global=is_global,
            cooldown=cooldown,
            metadata=metadata or {},
        )
        self._transitions[transition.id] = transition
        self._machine_transitions.setdefault(machine_id, []).append(transition.id)
        self._total_transitions_created += 1
        return transition.id

    def register_condition_handler(
        self,
        condition_name: str,
        handler: Callable[..., bool],
    ) -> None:
        """Register a custom condition handler function.

        Args:
            condition_name: The condition string this handler responds to.
            handler: Callable that returns True/False.
        """
        self._condition_handlers[condition_name] = handler

    # ------------------------------------------------------------------
    # State Machine Execution
    # ------------------------------------------------------------------

    def tick(self, machine_id: str) -> Dict[str, Any]:
        """Execute one tick of the state machine.

        Evaluates transitions from the current state (and global
        transitions), executes the first matching transition, and
        runs the update action of the current state.

        Args:
            machine_id: The machine to tick.

        Returns:
            Dict with current_state, transition_occurred, and actions.
        """
        _time_module.sleep(0.001)
        machine = self._machines.get(machine_id)
        if machine is None:
            return {"current_state": "", "transition_occurred": False, "actions": []}

        current_state = self._states.get(machine.current_state_id)
        if current_state is None:
            # Find initial state
            for sid in self._machine_states.get(machine_id, []):
                s = self._states.get(sid)
                if s and s.is_initial:
                    machine.current_state_id = sid
                    current_state = s
                    break
            if current_state is None:
                return {"current_state": "", "transition_occurred": False, "actions": []}

        now = _time_module.time()
        machine.time_in_state += 0.016  # Approximate 60fps delta
        machine.is_active = True
        actions_executed: List[str] = []

        # Collect candidate transitions
        transition_ids = self._machine_transitions.get(machine_id, [])
        candidates: List[AIStateTransition] = []

        for tid in transition_ids:
            trans = self._transitions.get(tid)
            if trans is None:
                continue
            if trans.is_global or trans.from_state_id == machine.current_state_id:
                # Cooldown check
                if trans.cooldown > 0 and now - trans.last_triggered < trans.cooldown:
                    continue
                candidates.append(trans)

        # Sort by priority (descending)
        candidates.sort(key=lambda t: t.priority, reverse=True)

        # Evaluate conditions
        transition_occurred = False
        for trans in candidates:
            if self._evaluate_transition_condition(trans, machine_id):
                # Execute exit actions of current state
                if current_state:
                    actions_executed.extend(current_state.exit_actions)

                # Transition
                machine.previous_state_id = machine.current_state_id
                machine.current_state_id = trans.to_state_id
                machine.time_in_state = 0.0
                machine.total_transitions += 1
                trans.last_triggered = now
                transition_occurred = True

                # Enter new state
                new_state = self._states.get(trans.to_state_id)
                if new_state:
                    actions_executed.extend(new_state.enter_actions)
                    # If new state has a sub-machine, activate it
                    if new_state.sub_machine_id:
                        sub = self._machines.get(new_state.sub_machine_id)
                        if sub:
                            sub.is_active = True
                break

        # Run update actions of current state
        current_state = self._states.get(machine.current_state_id)
        if current_state and not transition_occurred:
            actions_executed.extend(current_state.update_actions)

        # Tick sub-machine if active
        if current_state and current_state.sub_machine_id:
            sub = self._machines.get(current_state.sub_machine_id)
            if sub and sub.is_active:
                sub_result = self.tick(current_state.sub_machine_id)
                actions_executed.extend(sub_result.get("actions", []))

        return {
            "current_state": current_state.name if current_state else "",
            "current_state_id": machine.current_state_id,
            "previous_state_id": machine.previous_state_id,
            "transition_occurred": transition_occurred,
            "time_in_state": machine.time_in_state,
            "actions": actions_executed,
        }

    def _evaluate_transition_condition(
        self,
        transition: AIStateTransition,
        machine_id: str,
    ) -> bool:
        """Evaluate a transition's condition string."""
        cond = transition.condition

        if not cond:
            return True

        if cond == "always":
            return True
        if cond == "never":
            return False

        # Check registered handlers
        if cond in self._condition_handlers:
            try:
                return self._condition_handlers[cond](machine_id=machine_id)
            except Exception:
                return False

        # Built-in conditions
        if cond.startswith("time_in_state >"):
            try:
                threshold = float(cond.split(">")[1].strip())
                machine = self._machines.get(machine_id)
                if machine:
                    return machine.time_in_state > threshold
            except (ValueError, IndexError):
                pass

        if cond.startswith("random_"):
            try:
                prob = float(cond.split("_")[1]) / 100.0
                return random.random() < prob
            except (ValueError, IndexError):
                pass

        if cond == "sub_machine_done":
            machine = self._machines.get(machine_id)
            if machine:
                current = self._states.get(machine.current_state_id)
                if current and current.sub_machine_id:
                    sub = self._machines.get(current.sub_machine_id)
                    if sub:
                        return not sub.is_active
            return False

        # Default: condition not met
        return False

    def force_transition(
        self,
        machine_id: str,
        to_state_id: str,
    ) -> bool:
        """Force an immediate transition to a target state.

        Args:
            machine_id: Target machine identifier.
            to_state_id: Target state to transition to.

        Returns:
            True if the transition was performed.
        """
        _time_module.sleep(0.001)
        machine = self._machines.get(machine_id)
        if machine is None:
            return False
        if to_state_id not in self._states:
            return False

        current = self._states.get(machine.current_state_id)
        if current:
            # Execute exit actions
            pass  # Actions are symbolic — handled by external systems

        machine.previous_state_id = machine.current_state_id
        machine.current_state_id = to_state_id
        machine.time_in_state = 0.0
        machine.total_transitions += 1
        return True

    # ------------------------------------------------------------------
    # Visualization
    # ------------------------------------------------------------------

    def export_visualization_data(self, machine_id: str) -> Dict[str, Any]:
        """Export state machine data for visualization.

        Returns a dict with states and transitions suitable for
        rendering a state machine graph.

        Args:
            machine_id: Target machine identifier.

        Returns:
            Dict with 'states' and 'transitions' lists.
        """
        _time_module.sleep(0.001)
        machine = self._machines.get(machine_id)
        if machine is None:
            return {"states": [], "transitions": []}

        state_ids = self._machine_states.get(machine_id, [])
        transition_ids = self._machine_transitions.get(machine_id, [])

        vis_states = []
        for sid in state_ids:
            state = self._states.get(sid)
            if state is None:
                continue
            vis_states.append({
                "id": state.id,
                "name": state.name,
                "is_initial": state.is_initial,
                "is_current": state.id == machine.current_state_id,
                "has_sub_machine": bool(state.sub_machine_id),
                "tags": list(state.tags),
            })

        vis_transitions = []
        for tid in transition_ids:
            trans = self._transitions.get(tid)
            if trans is None:
                continue
            from_name = ""
            to_name = ""
            if trans.from_state_id in self._states:
                from_name = self._states[trans.from_state_id].name
            if trans.to_state_id in self._states:
                to_name = self._states[trans.to_state_id].name
            vis_transitions.append({
                "id": trans.id,
                "name": trans.name,
                "from": trans.from_state_id,
                "to": trans.to_state_id,
                "from_name": from_name,
                "to_name": to_name,
                "condition": trans.condition,
                "priority": trans.priority,
                "is_global": trans.is_global,
                "cooldown": trans.cooldown,
            })

        return {
            "machine_id": machine_id,
            "machine_name": machine.name,
            "current_state_id": machine.current_state_id,
            "states": vis_states,
            "transitions": vis_transitions,
        }

    def export_dot(self, machine_id: str) -> str:
        """Export state machine as Graphviz DOT format string.

        Args:
            machine_id: Target machine identifier.

        Returns:
            A DOT format string for graph rendering.
        """
        machine = self._machines.get(machine_id)
        if machine is None:
            return "digraph {}"

        state_ids = self._machine_states.get(machine_id, [])
        transition_ids = self._machine_transitions.get(machine_id, [])

        lines = [f'digraph "{machine.name}" {{']
        lines.append('  rankdir=LR;')
        lines.append('  node [shape=ellipse, style=filled, fillcolor=lightblue];')

        for sid in state_ids:
            state = self._states.get(sid)
            if state is None:
                continue
            color = "lightgreen" if state.id == machine.current_state_id else "lightblue"
            style = "filled,bold" if state.id == machine.current_state_id else "filled"
            lines.append(
                f'  "{sid}" [label="{state.name}", fillcolor={color}, style={style}];'
            )

        for tid in transition_ids:
            trans = self._transitions.get(tid)
            if trans is None:
                continue
            label = trans.name
            if trans.condition:
                label += f" [{trans.condition}]"
            lines.append(
                f'  "{trans.from_state_id}" -> "{trans.to_state_id}" [label="{label}"];'
            )

        lines.append("}")
        return "\n".join(lines)

    # ------------------------------------------------------------------
    # Query
    # ------------------------------------------------------------------

    def get_machine(self, machine_id: str) -> Optional[AIStateMachine]:
        """Get a state machine by ID."""
        return self._machines.get(machine_id)

    def get_state(self, state_id: str) -> Optional[AIState]:
        """Get a state by ID."""
        return self._states.get(state_id)

    def get_transition(self, transition_id: str) -> Optional[AIStateTransition]:
        """Get a transition by ID."""
        return self._transitions.get(transition_id)

    def list_machines(self) -> List[AIStateMachine]:
        """List all state machines."""
        return list(self._machines.values())

    def list_states(self, machine_id: str) -> List[AIState]:
        """List all states in a machine."""
        state_ids = self._machine_states.get(machine_id, [])
        return [self._states[sid] for sid in state_ids if sid in self._states]

    def list_transitions(self, machine_id: str) -> List[AIStateTransition]:
        """List all transitions in a machine."""
        trans_ids = self._machine_transitions.get(machine_id, [])
        return [self._transitions[tid] for tid in trans_ids if tid in self._transitions]

    def remove_machine(self, machine_id: str) -> bool:
        """Remove a state machine and all its states and transitions."""
        if machine_id not in self._machines:
            return False
        state_ids = self._machine_states.pop(machine_id, [])
        for sid in state_ids:
            self._states.pop(sid, None)
        trans_ids = self._machine_transitions.pop(machine_id, [])
        for tid in trans_ids:
            self._transitions.pop(tid, None)
        del self._machines[machine_id]
        return True

    def remove_state(self, state_id: str) -> bool:
        """Remove a state from its machine."""
        if state_id not in self._states:
            return False
        state = self._states[state_id]
        machine_id = state.machine_id
        if machine_id in self._machine_states:
            if state_id in self._machine_states[machine_id]:
                self._machine_states[machine_id].remove(state_id)
        # Remove transitions referencing this state
        trans_ids = self._machine_transitions.get(machine_id, [])
        to_remove = []
        for tid in trans_ids:
            trans = self._transitions.get(tid)
            if trans and (trans.from_state_id == state_id or trans.to_state_id == state_id):
                to_remove.append(tid)
        for tid in to_remove:
            self._transitions.pop(tid, None)
            if tid in trans_ids:
                trans_ids.remove(tid)
        del self._states[state_id]
        return True

    def get_stats(self) -> Dict[str, Any]:
        """Get state machine system statistics."""
        active_machines = sum(1 for m in self._machines.values() if m.is_active)
        return {
            "total_machines": len(self._machines),
            "total_machines_created": self._total_machines_created,
            "total_states": len(self._states),
            "total_states_created": self._total_states_created,
            "total_transitions": len(self._transitions),
            "total_transitions_created": self._total_transitions_created,
            "active_machines": active_machines,
            "registered_condition_handlers": len(self._condition_handlers),
        }

    def reset(self) -> None:
        """Reset all state machine data."""
        self._machines.clear()
        self._states.clear()
        self._transitions.clear()
        self._machine_states.clear()
        self._machine_transitions.clear()
        self._condition_handlers.clear()
        self._total_machines_created = 0
        self._total_states_created = 0
        self._total_transitions_created = 0


