class GameAISystem:
    """Central AI management system for the SparkLabs game engine.

    Owns and coordinates all AI subsystems: navmesh generation,
    pathfinding, behavior trees, perception, and state machines.
    Manages agent lifecycle, AI update scheduling with performance
    budgeting, AI debugging and visualization, and reusable AI
    profile management.

    Architecture:
      GameAISystem (singleton)
        |-- navmesh_generator: NavMeshGenerator
        |-- pathfinding_engine: PathfindingEngine
        |-- behavior_runtime: BehaviorTreeRuntime
        |-- perception_system: AIPerceptionSystem
        |-- state_machine: StateMachineAI
        |-- agents: Dict[str, AIAgent]
        |-- profiles: Dict[str, AIProfile]

    Usage:
        ai = get_ai_system()
        profile_id = ai.create_profile("guard", vision_range=60, aggression=0.7)
        agent_id = ai.spawn_agent("guard_01", profile_id=profile_id)
        ai.update(0.016)
    """

    _instance: Optional["GameAISystem"] = None
    _lock: threading.RLock = threading.RLock()

    MAX_AGENTS: int = DEFAULT_MAX_AGENTS
    MAX_PROFILES: int = 256
    GLOBAL_AI_BUDGET_MS: float = DEFAULT_AI_BUDGET_MS * 10

    def __new__(cls) -> "GameAISystem":
        if cls._instance is None:
            with cls._lock:
                if cls._instance is None:
                    instance = super().__new__(cls)
                    instance._initialized = False
                    cls._instance = instance
        return cls._instance

    def __init__(self) -> None:
        if getattr(self, "_initialized", False):
            return
        self.navmesh_generator: NavMeshGenerator = NavMeshGenerator()
        self.pathfinding_engine: PathfindingEngine = PathfindingEngine()
        self.behavior_runtime: BehaviorTreeRuntime = BehaviorTreeRuntime()
        self.perception_system: AIPerceptionSystem = AIPerceptionSystem()
        self.state_machine: StateMachineAI = StateMachineAI()
        self._agents: Dict[str, AIAgent] = {}
        self._profiles: Dict[str, AIProfile] = {}
        self._total_agents_spawned: int = 0
        self._total_profiles_created: int = 0
        self._total_ticks: int = 0
        self._total_cpu_ms: float = 0.0
        self._is_debug_visualization_enabled: bool = False
        self._debug_overlay_data: Dict[str, Any] = {}
        self._initialized = True

    @classmethod
    def get_instance(cls) -> "GameAISystem":
        return cls()

    # ------------------------------------------------------------------
    # Agent Lifecycle
    # ------------------------------------------------------------------

    def spawn_agent(
        self,
        name: str,
        profile_id: str = "",
        position: Tuple[float, float, float] = (0.0, 0.0, 0.0),
        rotation: Tuple[float, float, float] = (0.0, 0.0, 0.0),
        tags: Optional[List[str]] = None,
    ) -> str:
        """Spawn a new AI agent in the world.

        Creates an AIAgent, applies profile settings if a profile_id
        is provided, registers the agent with the perception system,
        and activates it.

        Args:
            name: Display name for the agent.
            profile_id: Optional AI profile to apply.
            position: World-space spawn position.
            rotation: Initial rotation (pitch, yaw, roll).
            tags: Optional tag list.

        Returns:
            The agent ID.

        Raises:
            RuntimeError: If the agent limit is exceeded.
        """
        _time_module.sleep(0.001)
        if len(self._agents) >= self.MAX_AGENTS:
            raise RuntimeError(
                f"AI agent limit reached ({self.MAX_AGENTS})"
            )

        agent = AIAgent(
            name=name,
            position=position,
            rotation=rotation,
            tags=tags or [],
        )

        # Apply profile if provided
        profile = self._profiles.get(profile_id) if profile_id else None
        if profile:
            agent.profile_id = profile_id
            agent.behavior_tree_id = profile.behavior_tree_id
            agent.state_machine_id = profile.state_machine_id
            agent.movement_speed = profile.movement_speed
            agent.rotation_speed = profile.rotation_speed
            agent.tick_rate = profile.tick_rate

            # Register with perception system
            self.perception_system.register_agent(
                agent_id=agent.id,
                vision_range=profile.vision_range,
                vision_angle=profile.vision_angle,
                hearing_range=profile.hearing_range,
            )

        agent.agent_state = AIAgentState.ACTIVE
        self._agents[agent.id] = agent
        self._total_agents_spawned += 1
        return agent.id

    def despawn_agent(self, agent_id: str) -> bool:
        """Despawn and remove an AI agent.

        Args:
            agent_id: The agent to remove.

        Returns:
            True if the agent was found and removed.
        """
        if agent_id not in self._agents:
            return False
        agent = self._agents[agent_id]
        agent.agent_state = AIAgentState.TERMINATED
        self.perception_system.unregister_agent(agent_id)
        del self._agents[agent_id]
        return True

    def suspend_agent(self, agent_id: str) -> bool:
        """Suspend an agent (pause AI processing).

        Args:
            agent_id: The agent to suspend.

        Returns:
            True if the agent was found and suspended.
        """
        agent = self._agents.get(agent_id)
        if agent is None:
            return False
        agent.agent_state = AIAgentState.SUSPENDED
        return True

    def resume_agent(self, agent_id: str) -> bool:
        """Resume a suspended agent.

        Args:
            agent_id: The agent to resume.

        Returns:
            True if the agent was found and resumed.
        """
        agent = self._agents.get(agent_id)
        if agent is None:
            return False
        agent.agent_state = AIAgentState.ACTIVE
        return True

    def get_agent(self, agent_id: str) -> Optional[AIAgent]:
        """Get an agent by ID."""
        return self._agents.get(agent_id)

    def list_agents(
        self,
        state_filter: Optional[str] = None,
        tag_filter: Optional[str] = None,
    ) -> List[AIAgent]:
        """List agents, optionally filtered by state or tag.

        Args:
            state_filter: Optional AIAgentState value to filter by.
            tag_filter: Optional tag to filter by.

        Returns:
            Filtered list of agents.
        """
        agents = list(self._agents.values())
        if state_filter:
            agents = [a for a in agents if a.agent_state.value == state_filter]
        if tag_filter:
            agents = [a for a in agents if tag_filter in a.tags]
        return agents

