    def _check_entity_performance(
        self, entity_id: str, data: Dict[str, Any], items: List[FeedbackItem],
    ) -> None:
        draw_calls = data.get("draw_calls", 0)
        if draw_calls > _DRAW_CALL_CRITICAL:
            items.append(self._make_feedback(
                FeedbackCategory.PERFORMANCE, FeedbackSeverity.CRITICAL,
                AnalysisScope.ENTITY, "Excessive Per-Entity Draw Calls",
                f"Entity generates {draw_calls} draw calls.",
                "Merge sub-meshes, reduce material count, or use GPU instancing.",
                entity_id,
            ))

        memory_kb = data.get("memory_kb", 0)
        if memory_kb > 51200:  # 50 MB
            items.append(self._make_feedback(
                FeedbackCategory.PERFORMANCE, FeedbackSeverity.WARNING,
                AnalysisScope.ENTITY, "High Per-Entity Memory",
                f"Entity uses {memory_kb / 1024:.1f} MB of memory.",
                "Reduce texture resolution or simplify mesh complexity.",
                entity_id,
            ))

        tex_width = data.get("texture_width", 0)
        tex_height = data.get("texture_height", 0)
        if tex_width > _TEXTURE_DIM_WARNING or tex_height > _TEXTURE_DIM_WARNING:
            items.append(self._make_feedback(
                FeedbackCategory.PERFORMANCE, FeedbackSeverity.WARNING,
                AnalysisScope.ENTITY, "Large Textures on Entity",
                f"Entity uses textures up to {tex_width}x{tex_height}.",
                "Downscale textures or use mipmap chains.",
                entity_id,
            ))

    def _check_entity_visual(
        self, entity_id: str, data: Dict[str, Any], items: List[FeedbackItem],
    ) -> None:
        contrast = data.get("contrast_ratio", 10.0)
        if contrast < _CONTRAST_RATIO_MIN:
            items.append(self._make_feedback(
                FeedbackCategory.VISUAL, FeedbackSeverity.WARNING,
                AnalysisScope.ENTITY, "Low Color Contrast",
                f"Entity color contrast ratio is {contrast:.1f}.",
                "Adjust text/foreground colors for better readability against background.",
                entity_id,
            ))

        if data.get("missing_texture", False):
            items.append(self._make_feedback(
                FeedbackCategory.VISUAL, FeedbackSeverity.ISSUE,
                AnalysisScope.ENTITY, "Missing Texture",
                "Entity references a texture that cannot be resolved.",
                "Assign a valid texture or use a placeholder fallback.",
                entity_id,
                auto_fixable=True,
            ))

        if data.get("overlapping_ui", False):
            items.append(self._make_feedback(
                FeedbackCategory.VISUAL, FeedbackSeverity.SUGGESTION,
                AnalysisScope.ENTITY, "UI Overlap Detected",
                "This UI element overlaps with another element.",
                "Adjust anchor offsets or use a layout group.",
                entity_id,
            ))

    def _check_entity_gameplay(
        self, entity_id: str, data: Dict[str, Any], items: List[FeedbackItem],
    ) -> None:
        stats = data.get("stats", {})
        if stats:
            attack = stats.get("attack", 0)
            defense = stats.get("defense", 0)
            health = stats.get("health", 0)
            values = [v for v in [attack, defense, health] if v > 0]
            if len(values) >= 2:
                mean_val = sum(values) / len(values)
                variance = sum((v - mean_val) ** 2 for v in values) / len(values)
                if variance > (mean_val ** 2) * 0.25:
                    items.append(self._make_feedback(
                        FeedbackCategory.BALANCE, FeedbackSeverity.RECOMMENDATION,
                        AnalysisScope.ENTITY, "Unbalanced Entity Stats",
                        f"Entity stats have high variance (mean={mean_val:.1f}).",
                        "Review stat distribution for competitive balance.",
                        entity_id,
                    ))

        if data.get("spawn_rate_per_min", 0) > 60:
            items.append(self._make_feedback(
                FeedbackCategory.GAMEPLAY, FeedbackSeverity.WARNING,
                AnalysisScope.ENTITY, "High Spawn Rate",
                "This spawner produces entities at a very high rate.",
                "Add a spawn cap or increase cooldown between waves.",
                entity_id,
            ))

    def _check_entity_accessibility(
        self, entity_id: str, data: Dict[str, Any], items: List[FeedbackItem],
    ) -> None:
        font_size = data.get("font_size", 16)
        if 0 < font_size < _FONT_SIZE_MIN:
            items.append(self._make_feedback(
                FeedbackCategory.ACCESSIBILITY, FeedbackSeverity.WARNING,
                AnalysisScope.ENTITY, "Small Font Size",
                f"Text element uses {font_size}px font size.",
                "Increase to at least 14px for readability. Support text scaling.",
                entity_id,
            ))

        if data.get("color_only_indicator", False):
            items.append(self._make_feedback(
                FeedbackCategory.ACCESSIBILITY, FeedbackSeverity.RECOMMENDATION,
                AnalysisScope.ENTITY, "Color-Only Indicator",
                "This element conveys information using color alone.",
                "Add an icon, pattern, or text label alongside the color cue.",
                entity_id,
            ))

        if data.get("is_interactive", False) and not data.get("has_audio_cue", False):
            items.append(self._make_feedback(
                FeedbackCategory.ACCESSIBILITY, FeedbackSeverity.SUGGESTION,
                AnalysisScope.ENTITY, "Missing Audio Cue",
                "Interactive element lacks an audio cue for screen reader or feedback.",
                "Add a hover/click sound for auditory confirmation.",
                entity_id,
            ))

    def _check_entity_code(
        self, entity_id: str, data: Dict[str, Any], items: List[FeedbackItem],
    ) -> None:
        unconnected = data.get("unconnected_events", 0)
        if unconnected > 0:
            items.append(self._make_feedback(
                FeedbackCategory.CODE, FeedbackSeverity.WARNING,
                AnalysisScope.ENTITY, "Unconnected Events",
                f"{unconnected} event(s) on this entity have no listeners.",
                "Connect event handlers or remove unused event bindings.",
                entity_id,
            ))

        missing = data.get("missing_references", 0)
        if missing > 0:
            items.append(self._make_feedback(
                FeedbackCategory.CODE, FeedbackSeverity.ISSUE,
                AnalysisScope.ENTITY, "Dangling References",
                f"{missing} references point to missing or deleted entities.",
                "Update references or set up null guards.",
                entity_id,
            ))

    # ------------------------------------------------------------------
    # Feedback Lifecycle
    # ------------------------------------------------------------------

    def apply_fix(self, feedback_id: str) -> bool:
        with self._lock:
            item = self._feedback_items.get(feedback_id)
            if item is None:
                return False
            if item.applied:
                return False
            item.applied = True
            self._total_fixes_applied += 1
            return True

    def dismiss_feedback(self, feedback_id: str) -> bool:
        with self._lock:
            item = self._feedback_items.get(feedback_id)
            if item is None:
                return False
            self._dismissed_ids.add(feedback_id)
            return True

    # ------------------------------------------------------------------
    # Query Methods
    # ------------------------------------------------------------------

    def get_feedback(
        self,
        category: Optional[FeedbackCategory] = None,
        severity: Optional[FeedbackSeverity] = None,
        scope: Optional[AnalysisScope] = None,
        applied: Optional[bool] = None,
    ) -> List[FeedbackItem]:
        with self._lock:
            result = []
            for item in self._feedback_items.values():
                if item.feedback_id in self._dismissed_ids:
                    continue
                if category is not None and item.category != category:
                    continue
                if severity is not None and item.severity != severity:
                    continue
                if scope is not None and item.scope != scope:
                    continue
                if applied is not None and item.applied != applied:
                    continue
                result.append(item)
            result.sort(key=lambda i: _SEVERITY_WEIGHT.get(i.severity.value, 0), reverse=True)
            return result

    def get_feedback_summary(self) -> Dict[str, Any]:
        with self._lock:
            active_items = [
                i for i in self._feedback_items.values()
                if i.feedback_id not in self._dismissed_ids
            ]
            category_counts: Dict[str, int] = {}
            severity_counts: Dict[str, int] = {}
            scope_counts: Dict[str, int] = {}
            applied_count = 0
            auto_fixable_count = 0

            for item in active_items:
                cat = item.category.value
                sev = item.severity.value
                scp = item.scope.value
                category_counts[cat] = category_counts.get(cat, 0) + 1
                severity_counts[sev] = severity_counts.get(sev, 0) + 1
                scope_counts[scp] = scope_counts.get(scp, 0) + 1
                if item.applied:
                    applied_count += 1
                if item.auto_fixable:
                    auto_fixable_count += 1

            critical_count = severity_counts.get(FeedbackSeverity.CRITICAL.value, 0)
            issue_count = severity_counts.get(FeedbackSeverity.ISSUE.value, 0)

            return {
                "total_active": len(active_items),
                "total_generated": self._total_feedback_generated,
                "total_dismissed": len(self._dismissed_ids),
                "applied": applied_count,
                "auto_fixable": auto_fixable_count,
                "critical_count": critical_count,
                "issue_count": issue_count,
                "by_category": category_counts,
                "by_severity": severity_counts,
                "by_scope": scope_counts,
                "focused_categories": sorted(
                    category_counts, key=lambda k: category_counts[k], reverse=True
                )[:3],
            }

    def get_stats(self) -> Dict[str, Any]:
        with self._lock:
            return {
                "total_analyses": self._total_analyses,
                "total_feedback_generated": self._total_feedback_generated,
                "total_fixes_applied": self._total_fixes_applied,
                "total_rules": len(self._rules),
                "enabled_rules": len([r for r in self._rules.values() if r.enabled]),
                "snapshots_stored": len(self._snapshots),
                "feedback_stored": len(self._feedback_items),
                "dismissed": len(self._dismissed_ids),
                "last_analysis_time": self._last_analysis_time,
            }

    # ------------------------------------------------------------------
    # Internal Helpers
    # ------------------------------------------------------------------

    def _make_feedback(
        self,
        category: FeedbackCategory,
        severity: FeedbackSeverity,
        scope: AnalysisScope,
        title: str,
        description: str,
        suggestion: str,
        source_entity: str = "",
        auto_fixable: bool = False,
    ) -> FeedbackItem:
        item = FeedbackItem(
            category=category,
            severity=severity,
            scope=scope,
            title=title,
            description=description,
            suggestion=suggestion,
            source_entity=source_entity,
            auto_fixable=auto_fixable,
        )
        return item

    def _record_analysis(
        self,
        scope: AnalysisScope,
        entities_analyzed: int,
        metrics: Dict[str, Any],
        duration_ms: float,
        items: List[FeedbackItem],
    ) -> None:
        with self._lock:
            self._total_analyses += 1
            self._last_analysis_time = _time_module.time()

            for item in items:
                self._feedback_items[item.feedback_id] = item
                self._total_feedback_generated += 1

                idx_key = f"{item.category.value}:{item.scope.value}"
                if idx_key not in self._feedback_index:
                    self._feedback_index[idx_key] = []
                self._feedback_index[idx_key].append(item.feedback_id)

            snapshot = AnalysisSnapshot(
                scope=scope,
                entities_analyzed=entities_analyzed,
                metrics={
                    k: v for k, v in metrics.items()
                    if isinstance(v, (int, float, str, bool, list, dict))
                },
                duration_ms=round(duration_ms, 2),
            )
            self._snapshots.append(snapshot)

    # ------------------------------------------------------------------
    # Serialization
    # ------------------------------------------------------------------

    def export_feedback(self, include_dismissed: bool = False) -> List[Dict[str, Any]]:
        with self._lock:
            result = []
            for item in self._feedback_items.values():
                if not include_dismissed and item.feedback_id in self._dismissed_ids:
                    continue
                result.append(item.to_dict())
            return result

    def export_rules(self) -> List[Dict[str, Any]]:
        with self._lock:
            return [
                {
                    "rule_id": r.rule_id,
                    "name": r.name,
                    "category": r.category.value,
                    "condition_template": r.condition_template,
                    "suggestion_template": r.suggestion_template,
                    "enabled": r.enabled,
                    "priority": r.priority,
                }
                for r in self._rules.values()
            ]

    def export_json(self, indent: int = 2) -> str:
        data = {
            "stats": self.get_stats(),
            "summary": self.get_feedback_summary(),
            "feedback": self.export_feedback(include_dismissed=False),
        }
        return json.dumps(data, indent=indent)

    def clear_feedback(self) -> int:
        with self._lock:
            count = len(self._feedback_items)
            self._feedback_items.clear()
            self._feedback_index.clear()
            self._dismissed_ids.clear()
            self._snapshots.clear()
            return count


