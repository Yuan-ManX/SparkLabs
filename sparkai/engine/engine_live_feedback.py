@dataclass
class FeedbackItem:
    """A single piece of feedback generated by the analysis engine."""

    feedback_id: str = field(default_factory=lambda: uuid.uuid4().hex)
    category: FeedbackCategory = FeedbackCategory.PERFORMANCE
    severity: FeedbackSeverity = FeedbackSeverity.SUGGESTION
    scope: AnalysisScope = AnalysisScope.ENTITY
    title: str = ""
    description: str = ""
    suggestion: str = ""
    source_entity: str = ""
    auto_fixable: bool = False
    applied: bool = False
    created_at: float = field(default_factory=_time_module.time)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "feedback_id": self.feedback_id,
            "category": self.category.value,
            "severity": self.severity.value,
            "scope": self.scope.value,
            "title": self.title,
            "description": self.description,
            "suggestion": self.suggestion,
            "source_entity": self.source_entity,
            "auto_fixable": self.auto_fixable,
            "applied": self.applied,
            "created_at": self.created_at,
        }


@dataclass
class AnalysisSnapshot:
    """A time-stamped capture of analysis state and metrics."""

    snapshot_id: str = field(default_factory=lambda: uuid.uuid4().hex)
    scope: AnalysisScope = AnalysisScope.SCENE
    entities_analyzed: int = 0
    metrics: Dict[str, Any] = field(default_factory=dict)
    timestamp: float = field(default_factory=_time_module.time)
    duration_ms: float = 0.0

    def to_dict(self) -> Dict[str, Any]:
        return {
            "snapshot_id": self.snapshot_id,
            "scope": self.scope.value,
            "entities_analyzed": self.entities_analyzed,
            "metrics": self.metrics,
            "timestamp": self.timestamp,
            "duration_ms": self.duration_ms,
        }


@dataclass
class FeedbackRule:
    """A configurable heuristic rule for feedback detection."""

