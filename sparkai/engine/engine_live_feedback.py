# ---------------------------------------------------------------------------
# Live Feedback Engine (Singleton)
# ---------------------------------------------------------------------------


class LiveFeedbackEngine:
    """
    Real-time AI analysis engine that monitors the game being built and
    provides intelligent feedback, suggestions, and optimization
    recommendations during the development process.

    Usage:
        engine = LiveFeedbackEngine.get_instance()
        items = engine.analyze_entity("player_01", entity_data)
        summary = engine.get_feedback_summary()
    """

    _instance: Optional["LiveFeedbackEngine"] = None
    _lock: threading.RLock = threading.RLock()

    def __new__(cls) -> "LiveFeedbackEngine":
        if cls._instance is None:
            with cls._lock:
                if cls._instance is None:
                    instance = super().__new__(cls)
                    instance._initialized = False
                    cls._instance = instance
        return cls._instance

    @classmethod
    def get_instance(cls) -> "LiveFeedbackEngine":
        if cls._instance is None:
            cls()
        return cls._instance

    def __init__(self) -> None:
        if self._initialized:
            return
        self._initialized = True

        self._feedback_items: Dict[str, FeedbackItem] = {}
        self._feedback_index: Dict[str, List[str]] = {}
        self._rules: Dict[str, FeedbackRule] = {}
        self._snapshots: deque = deque(maxlen=100)
        self._dismissed_ids: set = set()

        self._total_analyses: int = 0
        self._total_feedback_generated: int = 0
        self._total_fixes_applied: int = 0
        self._last_analysis_time: float = 0.0

        self._initialize_builtin_rules()

    # ------------------------------------------------------------------
    # Rule Management
    # ------------------------------------------------------------------

    def _initialize_builtin_rules(self) -> None:
        for rule_def in _BUILTIN_RULES:
            self.register_rule(
                name=rule_def["name"],
                category=rule_def["category"],
                condition_template=rule_def["condition_template"],
                suggestion_template=rule_def["suggestion_template"],
                priority=rule_def["priority"],
            )

    def register_rule(
        self,
        name: str,
        category: FeedbackCategory,
        condition_template: str,
        suggestion_template: str,
        priority: int = 0,
    ) -> FeedbackRule:
        with self._lock:
            rule = FeedbackRule(
                name=name,
                category=category,
                condition_template=condition_template,
                suggestion_template=suggestion_template,
                priority=priority,
            )
            self._rules[rule.rule_id] = rule
            return rule

    def get_rules(
        self,
        category: Optional[FeedbackCategory] = None,
        enabled_only: bool = True,
    ) -> List[FeedbackRule]:
        with self._lock:
            result = list(self._rules.values())
            if category is not None:
                result = [r for r in result if r.category == category]
            if enabled_only:
                result = [r for r in result if r.enabled]
            result.sort(key=lambda r: r.priority, reverse=True)
            return result

    # ------------------------------------------------------------------
    # Analysis Methods
    # ------------------------------------------------------------------

    def analyze_entity(
        self, entity_id: str, entity_data: Dict[str, Any]
    ) -> List[FeedbackItem]:
        """
        Run heuristic checks on a single entity across all relevant
        categories: performance, visual, gameplay, accessibility, code.
        """
        start = _time_module.time()
        items: List[FeedbackItem] = []

        self._check_entity_performance(entity_id, entity_data, items)
        self._check_entity_visual(entity_id, entity_data, items)
        self._check_entity_gameplay(entity_id, entity_data, items)
        self._check_entity_accessibility(entity_id, entity_data, items)
        self._check_entity_code(entity_id, entity_data, items)

        duration_ms = (_time_module.time() - start) * 1000.0
        self._record_analysis(AnalysisScope.ENTITY, 1, entity_data, duration_ms, items)
        return items

    def analyze_scene(
        self, scene_id: str, scene_data: Dict[str, Any]
    ) -> List[FeedbackItem]:
        """
        Run heuristic checks on an entire scene: entity composition,
        spatial relationships, z-ordering, and overall complexity.
        """
        start = _time_module.time()
        items: List[FeedbackItem] = []

        entity_count = scene_data.get("entity_count", 0)
        entities = scene_data.get("entities", [])

        # Performance: entity count
        if entity_count > _ENTITY_COUNT_CRITICAL:
            items.append(self._make_feedback(
                FeedbackCategory.PERFORMANCE, FeedbackSeverity.CRITICAL,
                AnalysisScope.SCENE, "Excessive Scene Entities",
                f"Scene contains {entity_count} entities, far above the recommended maximum.",
                "Split the scene into multiple sub-scenes. Use spatial partitioning and object pooling.",
                scene_id,
            ))
        elif entity_count > _ENTITY_COUNT_WARNING:
            items.append(self._make_feedback(
                FeedbackCategory.PERFORMANCE, FeedbackSeverity.WARNING,
                AnalysisScope.SCENE, "High Entity Count",
                f"Scene has {entity_count} entities. Performance may degrade on lower-end devices.",
                "Consider object pooling, LOD culling, or reducing decorative entities.",
                scene_id,
            ))

        # Z-ordering conflicts
        z_conflicts = scene_data.get("z_conflicts", 0)
        if z_conflicts > 0:
            items.append(self._make_feedback(
                FeedbackCategory.VISUAL, FeedbackSeverity.WARNING,
                AnalysisScope.SCENE, "Z-Ordering Conflicts Detected",
                f"{z_conflicts} entities share overlapping z-positions causing rendering ambiguity.",
                "Assign distinct z-values or use explicit sorting layers.",
                scene_id,
            ))

        # Architecture: scene depth
        scene_depth = scene_data.get("scene_depth", 0)
        if scene_depth > _SCENE_DEPTH_MAX:
            items.append(self._make_feedback(
                FeedbackCategory.ARCHITECTURE, FeedbackSeverity.RECOMMENDATION,
                AnalysisScope.SCENE, "Deep Scene Hierarchy",
                f"Scene hierarchy depth is {scene_depth}, which complicates traversal.",
                "Flatten the entity tree or split logical groups into prefabs.",
                scene_id,
            ))

        # Visual: missing textures across entities
        missing_tex = scene_data.get("missing_texture_count", 0)
        if missing_tex > 0:
            items.append(self._make_feedback(
                FeedbackCategory.VISUAL, FeedbackSeverity.ISSUE,
                AnalysisScope.SCENE, "Missing Texture References",
                f"{missing_tex} texture references are unresolved in this scene.",
                "Import missing assets or assign default placeholder textures.",
                scene_id,
                auto_fixable=missing_tex <= 5,
            ))

        # Visual: overlapping UI
        ui_overlaps = scene_data.get("ui_overlaps", 0)
        if ui_overlaps > 0:
            items.append(self._make_feedback(
                FeedbackCategory.VISUAL, FeedbackSeverity.SUGGESTION,
                AnalysisScope.SCENE, "Overlapping UI Elements",
                f"{ui_overlaps} UI elements overlap and may be obscured.",
                "Review anchor points and layout constraints.",
                scene_id,
            ))

        # Code: unconnected events
        unconnected = scene_data.get("unconnected_events", 0)
        if unconnected > 0:
            items.append(self._make_feedback(
                FeedbackCategory.CODE, FeedbackSeverity.WARNING,
                AnalysisScope.SCENE, "Unconnected Events",
                f"{unconnected} events are fired but have no listeners.",
                "Wire up handlers or remove dead event dispatches.",
                scene_id,
            ))

        # Code: circular dependencies
        circular = scene_data.get("circular_dependencies", 0)
        if circular > 0:
            items.append(self._make_feedback(
                FeedbackCategory.CODE, FeedbackSeverity.CRITICAL,
                AnalysisScope.SCENE, "Circular Dependencies",
                f"{circular} circular dependency chains detected among scene entities.",
                "Break cycles by introducing shared interfaces or mediator patterns.",
                scene_id,
            ))

        # Architecture: component coupling
        coupling = scene_data.get("component_coupling", 0.0)
        if coupling > 0.8:
            items.append(self._make_feedback(
                FeedbackCategory.ARCHITECTURE, FeedbackSeverity.WARNING,
                AnalysisScope.SCENE, "High Component Coupling",
                f"Component coupling score is {coupling:.2f}.",
                "Decouple with event buses or dependency injection.",
                scene_id,
            ))

        # Architecture: hierarchy depth average
        avg_depth = scene_data.get("avg_hierarchy_depth", 0.0)
        if avg_depth < 1.5 and entity_count > 10:
            items.append(self._make_feedback(
                FeedbackCategory.ARCHITECTURE, FeedbackSeverity.SUGGESTION,
                AnalysisScope.SCENE, "Flat Entity Hierarchy",
                f"Average hierarchy depth is {avg_depth:.1f} with {entity_count} entities.",
                "Group related entities under logical parent containers.",
                scene_id,
            ))

        # Resource: duplicate assets
        duplicates = scene_data.get("duplicate_assets", 0)
        if duplicates > 0:
            items.append(self._make_feedback(
                FeedbackCategory.RESOURCE, FeedbackSeverity.RECOMMENDATION,
                AnalysisScope.SCENE, "Duplicate Assets",
                f"{duplicates} duplicate asset instances found.",
                "De-duplicate assets and use shared references to save memory.",
                scene_id,
                auto_fixable=duplicates <= 10,
            ))

        duration_ms = (_time_module.time() - start) * 1000.0
        self._record_analysis(
            AnalysisScope.SCENE, entity_count, scene_data, duration_ms, items,
        )
        return items

    def analyze_project(
        self, project_data: Dict[str, Any]
    ) -> List[FeedbackItem]:
        """
        Run heuristic checks at the project level: overall architecture,
        resource bundling, global settings, and cross-scene concerns.
        """
        start = _time_module.time()
        items: List[FeedbackItem] = []
        project_id = project_data.get("project_id", "project")

        # Resource: bundle size
        bundle_mb = project_data.get("bundle_size_mb", 0)
        if bundle_mb > 200:
            items.append(self._make_feedback(
                FeedbackCategory.RESOURCE, FeedbackSeverity.CRITICAL,
                AnalysisScope.PROJECT, "Excessive Bundle Size",
                f"Total bundle size is {bundle_mb} MB, which impacts download and load times.",
                "Compress textures, strip unused assets, and enable streaming for large levels.",
                project_id,
            ))
        elif bundle_mb > 100:
            items.append(self._make_feedback(
                FeedbackCategory.RESOURCE, FeedbackSeverity.WARNING,
                AnalysisScope.PROJECT, "Large Bundle Size",
                f"Bundle size ({bundle_mb} MB) is approaching recommended limits.",
                "Audit assets for compression opportunities and unused files.",
                project_id,
            ))

        # Architecture: scene count and organization
        scene_count = project_data.get("scene_count", 0)
        if scene_count > 50:
            items.append(self._make_feedback(
                FeedbackCategory.ARCHITECTURE, FeedbackSeverity.WARNING,
                AnalysisScope.PROJECT, "High Scene Count",
                f"Project contains {scene_count} scenes. Navigation and loading may become complex.",
                "Group related scenes into level packs and consider scene streaming.",
                project_id,
            ))

        # Code: global event connections
        global_events_unconnected = project_data.get("global_unconnected_events", 0)
        if global_events_unconnected > 0:
            items.append(self._make_feedback(
                FeedbackCategory.CODE, FeedbackSeverity.ISSUE,
                AnalysisScope.PROJECT, "Unconnected Global Events",
                f"{global_events_unconnected} global events have no listeners across all scenes.",
                "Clean up unused global events or connect intended handlers.",
                project_id,
            ))

        # Code: missing cross-scene references
        missing_refs = project_data.get("missing_cross_scene_refs", 0)
        if missing_refs > 0:
            items.append(self._make_feedback(
                FeedbackCategory.CODE, FeedbackSeverity.ISSUE,
                AnalysisScope.PROJECT, "Dangling Cross-Scene References",
                f"{missing_refs} references point to entities in unloaded or deleted scenes.",
                "Use persistent ID systems or lazy-load referenced entities.",
                project_id,
            ))

        # Optimization: global settings
        if not project_data.get("object_pooling_enabled", False):
            items.append(self._make_feedback(
                FeedbackCategory.OPTIMIZATION, FeedbackSeverity.RECOMMENDATION,
                AnalysisScope.PROJECT, "Object Pooling Not Enabled",
                "Global object pooling is disabled. Frequent allocations cause GC pressure.",
                "Enable object pooling for frequently spawned entities (bullets, particles, etc.).",
                project_id,
            ))

        if not project_data.get("texture_atlas_enabled", False):
            items.append(self._make_feedback(
                FeedbackCategory.OPTIMIZATION, FeedbackSeverity.RECOMMENDATION,
                AnalysisScope.PROJECT, "Texture Atlas Not Configured",
                "Sprite batching with texture atlases is not enabled.",
                "Configure a shared texture atlas to reduce draw calls.",
                project_id,
            ))

        # Accessibility: project-level settings
        if not project_data.get("color_blind_mode_supported", False):
            items.append(self._make_feedback(
                FeedbackCategory.ACCESSIBILITY, FeedbackSeverity.RECOMMENDATION,
                AnalysisScope.PROJECT, "No Color-Blind Support",
                "The project does not include color-blind friendly modes.",
                "Add configurable color palettes or pattern-based indicators.",
                project_id,
            ))

        if not project_data.get("subtitle_support", False):
            items.append(self._make_feedback(
                FeedbackCategory.ACCESSIBILITY, FeedbackSeverity.SUGGESTION,
                AnalysisScope.PROJECT, "No Subtitle System",
                "Subtitles or closed captions are not configured.",
                "Implement a subtitle system for dialogue and important audio cues.",
                project_id,
            ))

        # Balance: global economy if applicable
        economy_data = project_data.get("economy_settings", {})
        if economy_data:
            inflow = economy_data.get("currency_inflow_per_minute", 0.0)
            outflow = economy_data.get("currency_outflow_per_minute", 0.0)
            if outflow > 0 and inflow / outflow > 3.0:
                items.append(self._make_feedback(
                    FeedbackCategory.BALANCE, FeedbackSeverity.WARNING,
                    AnalysisScope.PROJECT, "Economy Inflation Risk",
                    f"Currency inflow ({inflow:.1f}/min) far exceeds outflow ({outflow:.1f}/min).",
                    "Reduce reward amounts or increase pricing for key items.",
                    project_id,
                ))

        duration_ms = (_time_module.time() - start) * 1000.0
        self._record_analysis(
            AnalysisScope.PROJECT, 0, project_data, duration_ms, items,
        )
        return items

    def analyze_runtime(
        self, runtime_metrics: Dict[str, Any]
    ) -> List[FeedbackItem]:
        """
        Analyze live runtime metrics: frame rate, memory, draw calls,
        entity counts, and gameplay flow indicators.
        """
        start = _time_module.time()
        items: List[FeedbackItem] = []
        runtime_id = runtime_metrics.get("session_id", "runtime")

        # Performance: frame rate
        fps = runtime_metrics.get("fps", 60)
        if fps < _FPS_CRITICAL_THRESHOLD:
            items.append(self._make_feedback(
                FeedbackCategory.PERFORMANCE, FeedbackSeverity.CRITICAL,
                AnalysisScope.RUNTIME, "Critical Frame Rate Drop",
                f"FPS is {fps:.0f}, well below playable threshold.",
                "Immediately reduce draw calls, disable expensive post-effects, enable LOD.",
                runtime_id,
            ))
        elif fps < _FPS_WARNING_THRESHOLD:
            items.append(self._make_feedback(
                FeedbackCategory.PERFORMANCE, FeedbackSeverity.WARNING,
                AnalysisScope.RUNTIME, "Low Frame Rate",
                f"FPS is {fps:.0f}. Player experience may be degraded.",
                "Profile draw calls and shader complexity. Reduce particle counts.",
                runtime_id,
            ))

        # Performance: draw calls
        draw_calls = runtime_metrics.get("draw_calls", 0)
        if draw_calls > _DRAW_CALL_CRITICAL:
            items.append(self._make_feedback(
                FeedbackCategory.PERFORMANCE, FeedbackSeverity.CRITICAL,
                AnalysisScope.RUNTIME, "Excessive Draw Calls",
                f"Draw calls ({draw_calls}) severely impact GPU performance.",
                "Batch sprites, merge static meshes, reduce unique materials.",
                runtime_id,
            ))
        elif draw_calls > _DRAW_CALL_WARNING:
            items.append(self._make_feedback(
                FeedbackCategory.PERFORMANCE, FeedbackSeverity.WARNING,
                AnalysisScope.RUNTIME, "High Draw Call Count",
                f"Draw calls ({draw_calls}) are approaching performance limits.",
                "Consider GPU instancing and texture atlasing.",
                runtime_id,
            ))

        # Performance: memory
        memory_mb = runtime_metrics.get("memory_usage_mb", 0)
        if memory_mb > _MEMORY_CRITICAL_MB:
            items.append(self._make_feedback(
                FeedbackCategory.PERFORMANCE, FeedbackSeverity.CRITICAL,
                AnalysisScope.RUNTIME, "Critical Memory Usage",
                f"Memory usage ({memory_mb} MB) risks out-of-memory crashes.",
                "Release unused assets, reduce texture sizes, enable asset streaming.",
                runtime_id,
            ))
        elif memory_mb > _MEMORY_WARNING_MB:
            items.append(self._make_feedback(
                FeedbackCategory.PERFORMANCE, FeedbackSeverity.WARNING,
                AnalysisScope.RUNTIME, "High Memory Usage",
                f"Memory usage ({memory_mb} MB) is elevated.",
                "Check for asset leaks and unpooled object allocations.",
                runtime_id,
            ))

        # Performance: entity count in active scene
        entity_count = runtime_metrics.get("active_entity_count", 0)
        if entity_count > _ENTITY_COUNT_CRITICAL:
            items.append(self._make_feedback(
                FeedbackCategory.PERFORMANCE, FeedbackSeverity.CRITICAL,
                AnalysisScope.RUNTIME, "Excessive Active Entities",
                f"{entity_count} entities active simultaneously.",
                "Enable frustum culling, deactivate off-screen entities, use LOD groups.",
                runtime_id,
            ))

        # Gameplay: spawn rate
        spawn_rate = runtime_metrics.get("spawns_per_minute", 0)
        if spawn_rate > 60:
            items.append(self._make_feedback(
                FeedbackCategory.GAMEPLAY, FeedbackSeverity.WARNING,
                AnalysisScope.RUNTIME, "High Spawn Rate",
                f"Spawn rate is {spawn_rate:.0f} per minute, potentially overwhelming.",
                "Introduce spawn caps, cooldowns, or wave-based spawning.",
                runtime_id,
            ))

        # Gameplay: reward frequency
        rewards = runtime_metrics.get("rewards_per_minute", 0.0)
        if 0 < rewards < 0.5:
            items.append(self._make_feedback(
                FeedbackCategory.GAMEPLAY, FeedbackSeverity.RECOMMENDATION,
                AnalysisScope.RUNTIME, "Low Reward Frequency",
                f"Only {rewards:.2f} rewards per minute. Players need more positive reinforcement.",
                "Add intermediate collectibles or milestone bonuses.",
                runtime_id,
            ))

        # Gameplay: difficulty indicators
        difficulty = runtime_metrics.get("difficulty_slope", 0.0)
        if difficulty > 1.5:
            items.append(self._make_feedback(
                FeedbackCategory.GAMEPLAY, FeedbackSeverity.WARNING,
                AnalysisScope.RUNTIME, "Steep Difficulty Ramp",
                f"Difficulty slope is {difficulty:.2f}. Players may hit a wall.",
                "Smooth the progression curve with gentler stat increases.",
                runtime_id,
            ))

        # Optimization: pool hit ratio
        pool_ratio = runtime_metrics.get("pool_hit_ratio", 1.0)
        if pool_ratio < 0.5:
            items.append(self._make_feedback(
                FeedbackCategory.OPTIMIZATION, FeedbackSeverity.RECOMMENDATION,
                AnalysisScope.RUNTIME, "Low Pool Hit Ratio",
                f"Object pool hit ratio is {pool_ratio:.2f}.",
                "Increase pool pre-warm sizes or adjust pool eviction policy.",
                runtime_id,
            ))

        # Optimization: unbatched sprites
        unbatched = runtime_metrics.get("unbatched_sprites", 0)
        if unbatched > 50:
            items.append(self._make_feedback(
                FeedbackCategory.OPTIMIZATION, FeedbackSeverity.RECOMMENDATION,
                AnalysisScope.RUNTIME, "Unbatched Sprites",
                f"{unbatched} sprites are rendered without batching.",
                "Consolidate into shared material groups or texture atlases.",
                runtime_id,
            ))

        duration_ms = (_time_module.time() - start) * 1000.0
        self._record_analysis(
            AnalysisScope.RUNTIME, 0, runtime_metrics, duration_ms, items,
        )
        return items

    def analyze_asset(
        self, asset_id: str, asset_data: Dict[str, Any]
    ) -> List[FeedbackItem]:
        """
        Analyze an individual asset for quality, performance, and resource issues.
        Checks texture dimensions, file size, format, and naming conventions.
        """
        start = _time_module.time()
        items: List[FeedbackItem] = []

        asset_type = asset_data.get("type", "unknown")

        # Performance: texture dimensions
        if asset_type in ("texture", "sprite", "image"):
            width = asset_data.get("width", 0)
            height = asset_data.get("height", 0)
            if width > _TEXTURE_DIM_CRITICAL or height > _TEXTURE_DIM_CRITICAL:
                items.append(self._make_feedback(
                    FeedbackCategory.PERFORMANCE, FeedbackSeverity.CRITICAL,
                    AnalysisScope.ASSET, "Texture Too Large",
                    f"Texture dimensions ({width}x{height}) exceed recommended maximum.",
                    "Downscale and use mipmaps. Split large textures into tiles if needed.",
                    asset_id,
                ))
            elif width > _TEXTURE_DIM_WARNING or height > _TEXTURE_DIM_WARNING:
                items.append(self._make_feedback(
                    FeedbackCategory.PERFORMANCE, FeedbackSeverity.WARNING,
                    AnalysisScope.ASSET, "Large Texture Dimensions",
                    f"Texture size ({width}x{height}) may impact performance on mobile.",
                    "Consider providing lower-resolution variants or compressing.",
                    asset_id,
                ))

        # Resource: file size
        size_kb = asset_data.get("size_kb", 0)
        size_mb = size_kb / 1024.0
        if size_mb > 50:
            items.append(self._make_feedback(
                FeedbackCategory.RESOURCE, FeedbackSeverity.WARNING,
                AnalysisScope.ASSET, "Large Asset File",
                f"Asset file size ({size_mb:.1f} MB) is large.",
                "Check for unnecessary detail. Use compressed formats where suitable.",
                asset_id,
            ))

        # Resource: format recommendations
        asset_format = asset_data.get("format", "").lower()
        if asset_type == "texture" and asset_format in ("bmp", "tiff"):
            items.append(self._make_feedback(
                FeedbackCategory.RESOURCE, FeedbackSeverity.RECOMMENDATION,
                AnalysisScope.ASSET, "Uncompressed Texture Format",
                f"Texture uses {asset_format} format without compression.",
                "Convert to PNG, WebP, or compressed DDS/ASTC for GPU.",
                asset_id,
            ))

        if asset_type == "audio" and asset_format == "wav" and size_mb > 5:
            items.append(self._make_feedback(
                FeedbackCategory.RESOURCE, FeedbackSeverity.RECOMMENDATION,
                AnalysisScope.ASSET, "Uncompressed Audio Asset",
                f"Audio asset is {size_mb:.1f} MB in WAV format.",
                "Convert to OGG, MP3, or ADPCM for in-game use.",
                asset_id,
            ))

        # Visual: naming convention
        asset_name = asset_data.get("name", "")
        if asset_name and " " in asset_name:
            items.append(self._make_feedback(
                FeedbackCategory.CODE, FeedbackSeverity.SUGGESTION,
                AnalysisScope.ASSET, "Asset Name Contains Spaces",
                f"Asset '{asset_name}' contains spaces in its name.",
                "Use underscores or camelCase for asset names to avoid cross-platform issues.",
                asset_id,
                auto_fixable=True,
            ))

        # Resource: unused asset detection
        if asset_data.get("reference_count", 1) == 0:
            items.append(self._make_feedback(
                FeedbackCategory.RESOURCE, FeedbackSeverity.SUGGESTION,
                AnalysisScope.ASSET, "Unreferenced Asset",
                f"Asset '{asset_name or asset_id}' is not referenced by any entity.",
                "Remove unused assets to reduce bundle size, or mark as intentionally kept.",
                asset_id,
            ))

        duration_ms = (_time_module.time() - start) * 1000.0
        self._record_analysis(
            AnalysisScope.ASSET, 1, asset_data, duration_ms, items,
        )
        return items

