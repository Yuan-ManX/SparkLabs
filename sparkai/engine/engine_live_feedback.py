class FeedbackSeverity(Enum):
    SUGGESTION = "suggestion"
    RECOMMENDATION = "recommendation"
    WARNING = "warning"
    ISSUE = "issue"
    CRITICAL = "critical"


class AnalysisScope(Enum):
    ENTITY = "entity"
    SCENE = "scene"
    PROJECT = "project"
    RUNTIME = "runtime"
    ASSET = "asset"


